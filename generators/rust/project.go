package rust

import (
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

// projectFiles scaffolds a buildable Cargo binary with a serde-json encode/decode
// harness over the selected Rust corelib: corelib-rs-no-std with only the wire
// types the schema needs (default), or the always-on std corelib-rs.
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	return []generator.File{
		{Path: "Cargo.toml", Content: []byte(g.cargoToml(s))},
		{Path: "src/main.rs", Content: g.harness(s)},
		{Path: "README.md", Content: []byte(rustReadme)},
	}
}

const rustReadme = "# Generated SofaBuffers Rust crate\n\n" +
	"`@generated by sofabgen`. Encode/decode harness over a Rust corelib —\n" +
	"`corelib-rs-no-std` (default) or `corelib-rs` (`corelib: rs`).\n\n" +
	"```sh\n# set the sofab path in Cargo.toml, then:\n" +
	"echo '{...}' | cargo run -- encode <Message>\n" +
	"cargo run -- decode <Message> < bytes.bin\n```\n"

func (g *gen) cargoToml(s *ir.Schema) string {
	// corelib-rs (std) has no Cargo features — every wire type is always compiled
	// in and the value type is always 64-bit. Its crate is published as the
	// package "SofaBuffers" (importable as `sofab`).
	dep := `sofab = { package = "SofaBuffers", path = "${SOFAB_RS_CORELIB}" }`
	if !g.std() {
		// corelib-rs-no-std: turn every feature off and re-enable exactly the wire
		// types this schema uses, so the footprint matches the messages (the point
		// of the no_std build). require!() in the module asserts the same set.
		feats := ""
		if caps := g.capabilities(s); len(caps) > 0 {
			quoted := make([]string, len(caps))
			for i, c := range caps {
				quoted[i] = `"` + c + `"`
			}
			feats = ", features = [" + strings.Join(quoted, ", ") + "]"
		}
		dep = `sofab = { path = "${SOFAB_RS_CORELIB}", default-features = false` + feats + ` }`
	}
	return `# @generated by sofabgen
[package]
name = "sofabuffers-generated"
version = "0.0.0"
edition = "2021"

[dependencies]
` + dep + `
serde = { version = "1", features = ["derive"] }
serde_json = "1"

[[bin]]
name = "harness"
path = "src/main.rs"
`
}

func (g *gen) harness(s *ir.Schema) []byte {
	f := &rfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	f.line("mod messages;")
	f.line("use messages::*;")
	f.line("use std::io::{Read, Write};")
	f.blank()
	f.line("fn main() {")
	f.line("    let args: Vec<String> = std::env::args().collect();")
	f.line("    let mode = args.get(1).map(|s| s.as_str()).unwrap_or(\"\");")
	f.line("    let name = args.get(2).map(|s| s.as_str()).unwrap_or(%q);", defaultMessage(s))
	f.line("    let mut input = Vec::new();")
	f.line("    std::io::stdin().read_to_end(&mut input).unwrap();")
	f.line("    match name {")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		f.line("        %q => {", m.Name)
		f.line("            if mode == \"encode\" {")
		f.line("                let obj: %s = serde_json::from_slice(&input).expect(\"json\");", mt)
		f.line("                std::io::stdout().write_all(&obj.encode()).unwrap();")
		f.line("            } else if mode == \"decode\" {")
		f.line("                let obj = %s::decode(&input);", mt)
		f.line("                println!(\"{}\", serde_json::to_string(&obj).unwrap());")
		f.line("            } else { eprintln!(\"unknown mode\"); std::process::exit(2); }")
		f.line("        }")
	}
	f.line("        _ => { eprintln!(\"unknown message\"); std::process::exit(2); }")
	f.line("    }")
	f.line("}")
	return f.bytes()
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}

