package rust

import (
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

// projectFiles scaffolds a buildable Cargo binary with a serde-json encode/decode
// harness over the selected Rust corelib: corelib-rs-no-std with only the wire
// types the schema needs (default), or the always-on std corelib-rs.
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	files := []generator.File{
		{Path: "Cargo.toml", Content: []byte(g.cargoToml(s))},
		{Path: "src/main.rs", Content: g.harness(s)},
		{Path: "README.md", Content: []byte(rustReadme)},
	}
	if g.noStd {
		// The no_std profile ships a lib crate root that carries the crate-level
		// #![no_std] (a bin cannot be no_std on a hosted target — it needs a panic
		// handler); the harness is a separate std bin over that lib.
		files = append(files, generator.File{Path: "src/lib.rs", Content: g.libRoot(s)})
	}
	return files
}

// libRoot is the crate root for the no_std profile: it applies #![no_std] (unless
// the `std` feature, which the harness needs, is on) and re-exports the message
// module. `extern crate alloc` is pulled only when allow_dynamic put an alloc
// container in the generated types.
func (g *gen) libRoot(s *ir.Schema) []byte {
	f := &rfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("// SPDX-License-Identifier: %s", g.license)
	}
	f.line("#![cfg_attr(not(feature = \"std\"), no_std)]")
	if g.usesAlloc(s) {
		f.line("extern crate alloc;")
	}
	f.line("mod message;")
	f.line("pub use message::*;")
	return f.bytes()
}

const rustReadme = "# Generated SofaBuffers Rust crate\n\n" +
	"`@generated by sofabgen`. Encode/decode harness over a Rust corelib -\n" +
	"`corelib-rs-no-std` (default) or `corelib-rs` (`corelib: rs`).\n\n" +
	"```sh\n# set the sofab path in Cargo.toml, then:\n" +
	"echo '{...}' | cargo run -- encode <Message>\n" +
	"cargo run -- decode <Message> < bytes.bin\n```\n"

func (g *gen) cargoToml(s *ir.Schema) string {
	// corelib-rs (std) has no Cargo features — every wire type is always compiled
	// in and the value type is always 64-bit. Its crate is published as the
	// package "sofa-buffers-corelib" (importable as `sofab`).
	dep := `sofab = { package = "sofa-buffers-corelib", path = "${SOFAB_RS_CORELIB}" }`
	if !g.std() {
		// corelib-rs-no-std: turn every feature off and re-enable exactly the wire
		// types this schema uses, so the footprint matches the messages (the point
		// of the no_std build). require!() in the module asserts the same set.
		feats := ""
		if caps := g.capabilities(s); len(caps) > 0 {
			quoted := make([]string, len(caps))
			for i, c := range caps {
				quoted[i] = `"` + c + `"`
			}
			feats = ", features = [" + strings.Join(quoted, ", ") + "]"
		}
		dep = `sofab = { package = "sofa-buffers-corelib-no-std", path = "${SOFAB_RS_CORELIB}", default-features = false` + feats + ` }`
	}
	if g.noStd {
		// Heap-free profile: a lib crate (the firmware artifact, buildable as
		// #![no_std] via `--no-default-features`) plus a std harness bin gated on the
		// `std` feature. serde is optional, pulled by `std` for the JSON harness.
		return `# @generated by sofabgen
[package]
name = "sofabuffers-generated"
version = "0.0.0"
edition = "2021"

[lib]
name = "sofabuffers_generated"
path = "src/lib.rs"

[[bin]]
name = "harness"
path = "src/main.rs"
required-features = ["std"]

[dependencies]
` + dep + `
heapless = { version = "0.8", default-features = false }
serde = { version = "1", default-features = false, features = ["derive"], optional = true }
serde_json = { version = "1", optional = true }

[features]
default = ["std"]
# std pulls serde + serde_json so the JSON harness builds; a bare-metal consumer
# uses --no-default-features for a genuinely #![no_std], heap-free lib.
std = ["serde", "dep:serde_json"]
serde = ["dep:serde", "heapless/serde"]
`
	}
	return `# @generated by sofabgen
[package]
name = "sofabuffers-generated"
version = "0.0.0"
edition = "2021"

[dependencies]
` + dep + `
serde = { version = "1", features = ["derive"] }
serde_json = "1"

[[bin]]
name = "harness"
path = "src/main.rs"
`
}

func (g *gen) harness(s *ir.Schema) []byte {
	f := &rfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	if g.noStd {
		// The message module is the crate's lib (src/lib.rs); this std harness bin
		// (built only with the `std` feature) consumes it as a dependency.
		f.line("use sofabuffers_generated::*;")
	} else {
		f.line("mod message;")
		f.line("use message::*;")
	}
	f.line("use std::io::{Read, Write};")
	f.blank()
	f.line("fn main() {")
	f.line("    let args: Vec<String> = std::env::args().collect();")
	f.line("    let mode = args.get(1).map(|s| s.as_str()).unwrap_or(\"\");")
	f.line("    let name = args.get(2).map(|s| s.as_str()).unwrap_or(%q);", defaultMessage(s))
	f.line("    let mut input = Vec::new();")
	f.line("    std::io::stdin().read_to_end(&mut input).unwrap();")
	f.line("    match name {")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		f.line("        %q => {", m.Name)
		f.line("            if mode == \"encode\" {")
		f.line("                let obj: %s = serde_json::from_slice(&input).expect(\"json\");", mt)
		f.line("                std::io::stdout().write_all(&obj.encode()).unwrap();")
		f.line("            } else if mode == \"decode\" {")
		f.line("                let obj = %s::decode(&input);", mt)
		f.line("                println!(\"{}\", serde_json::to_string(&obj).unwrap());")
		f.line("            } else { eprintln!(\"unknown mode\"); std::process::exit(2); }")
		f.line("        }")
	}
	f.line("        _ => { eprintln!(\"unknown message\"); std::process::exit(2); }")
	f.line("    }")
	f.line("}")
	return f.bytes()
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}

