// Package csharp is the C#/.NET throughput backend (PLAN §6.4): classes with
// Marshal over OStream and a flat-visitor decode (IVisitor) against corelib-cs.
// The corelib's IVisitor is flat (SequenceBegin/End, no child visitors), so
// decode is a (location, id) state machine with a location stack — mirroring the
// Rust backend.
package csharp

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

func init() { generator.Register(&Backend{}) }

// Backend implements generator.Backend for C#.
type Backend struct{}

func (*Backend) Lang() string { return "csharp" }

func (*Backend) Generate(s *ir.Schema, cfg map[string]any) ([]generator.File, error) {
	g := &gen{schema: s, ns: cfgString(cfg, "namespace", "Message"), banner: cfgString(cfg, "tool_banner", "sofabgen"), license: generator.LicenseID(cfg), limits: resolveLimits(cfg)}
	files := []generator.File{{Path: "Message.cs", Content: g.module(s)}}
	if cfgString(cfg, "emit", "sources") == "project" {
		files = append(files, g.projectFiles(s, cfg)...)
	}
	return files, nil
}

type gen struct {
	schema  *ir.Schema
	ns      string
	banner  string
	license string // SPDX id, "" to omit the header line
	limits  limitSet
}

// limitSet is the receiver-side decode-limit configuration (generator#102).
// Unlike the corelib-global backends (Go/Python/TypeScript), the C# guards are
// emitted per-field in the generated visitor and only on schema-unbounded
// fields, so each configured cap is used as-is (no raise to the largest schema
// bound): a schema-bounded field never meets a cap and keeps only its
// generator#100 schema-capacity guard. A configured key is still inert for a
// message whose reachable fields have no unbounded field of that kind
// (ir.Bounds, checked per visitor) — then no constant or guard is emitted.
type limitSet struct {
	arrayCount, stringLen, blobLen int64
	arrayHas, stringHas, blobHas   bool
}

// resolveLimits reads the max_dyn_* config keys (see limitSet).
func resolveLimits(cfg map[string]any) limitSet {
	var l limitSet
	l.arrayCount, l.arrayHas = cfgLimit(cfg, "max_dyn_array_count")
	l.stringLen, l.stringHas = cfgLimit(cfg, "max_dyn_string_len")
	l.blobLen, l.blobHas = cfgLimit(cfg, "max_dyn_blob_len")
	return l
}

type cfile struct{ b strings.Builder }

func (f *cfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}
func (f *cfile) blank()        { f.b.WriteByte('\n') }
func (f *cfile) bytes() []byte { return []byte(f.b.String()) }

func (g *gen) module(s *ir.Schema) []byte {
	f := &cfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("// SPDX-License-Identifier: %s", g.license)
	}
	f.line("using System;")
	f.line("using System.Collections.Generic;")
	f.line("using System.Text;")
	f.line("using sofab;")
	f.blank()
	f.line("namespace %s;", g.ns)
	f.blank()

	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		switch nt.Category {
		case ir.CatEnum:
			g.emitEnum(f, nt)
		case ir.CatBitfield:
			g.emitBitfield(f, nt)
		}
	}
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitClass(f, g.typeName(key), nt.Summary, nt.Fields, false)
		}
	}
	for _, m := range s.Messages {
		g.emitClass(f, exported(m.Name), m.Summary, m.Fields, true)
	}
	return f.bytes()
}

func (g *gen) emitEnum(f *cfile, nt *ir.NamedType) {
	f.line("public enum %s : %s {", g.typeName(nt.Key), enumBacking(nt))
	for _, c := range nt.Consts {
		emitDoc(f, "    ", c.Description)
		f.line("    %s = %d,", exported(c.Name), c.Value)
	}
	f.line("}")
	f.blank()
}

func (g *gen) emitBitfield(f *cfile, nt *ir.NamedType) {
	f.line("[Flags]")
	f.line("public enum %s : %s {", g.typeName(nt.Key), bitfieldBacking(nt))
	for _, fl := range nt.Flags {
		emitDoc(f, "    ", flagDoc(fl))
		f.line("    %s = %d,", exported(fl.Name), uint64(1)<<uint(fl.Pos))
	}
	f.line("}")
	f.blank()
}

func (g *gen) emitClass(f *cfile, name, summary string, fields []*ir.Field, isMessage bool) {
	emitDoc(f, "", summary)
	f.line("public sealed class %s {", name)
	for _, fld := range fields {
		emitDoc(f, "    ", fieldDoc(fld))
		if fld.Deprecated {
			f.line("    [Obsolete]")
		}
		f.line("    public %s %s%s;", g.csType(fld), csIdent(fld.Name), g.csInit(fld))
	}
	f.blank()

	// Marshal. Generated marshal legitimately reads fields marked [Obsolete];
	// silence the CS0618 deprecation warning around that internal access.
	dep := hasDeprecatedDirect(fields)
	f.line("    public void Marshal(OStream os) {")
	if dep {
		f.line("#pragma warning disable 618 // internal access to a member marked [Obsolete]")
	}
	for _, fld := range fields {
		g.emitMarshal(f, fld)
	}
	if dep {
		f.line("#pragma warning restore 618")
	}
	f.line("    }")

	if isMessage {
		size, _ := g.maxSize(fields)
		f.line("    public const int MaxSize = %d;", size)
		f.line("    // Per-thread scratch buffer: Encode() marshals into it and returns an")
		f.line("    // exact-size copy, so the worst-case buffer is not re-allocated (and")
		f.line("    // zeroed) on every call. Do not call Encode() reentrantly from a")
		f.line("    // Marshal() override on the same thread.")
		f.line("    [ThreadStatic] private static byte[] _encScratch;")
		f.line("    public byte[] Encode() {")
		f.line("        var buf = _encScratch ??= new byte[MaxSize];")
		f.line("        var os = new OStream(buf);")
		f.line("        Marshal(os);")
		f.line("        var outp = new byte[os.BytesUsed];")
		f.line("        Array.Copy(buf, outp, os.BytesUsed);")
		f.line("        return outp;")
		f.line("    }")
		// Best-effort decode: kept for back-compat. It discards Feed's
		// DecodeStatus, so a truncated message is indistinguishable from a
		// COMPLETE one — prefer TryDecode when that verdict matters
		// (generator#105).
		f.line("    public static %s Decode(byte[] data) {", name)
		f.line("        var m = new %s();", name)
		f.line("        var v = new %sVisitor(m);", name)
		f.line("        new IStream().Feed(data, 0, data.Length, v);")
		f.line("        return m;")
		f.line("    }")
		// Status-surfacing one-shot decode (MESSAGE_SPEC §7): returns the
		// corelib's terminal DecodeStatus — Complete at a clean field boundary,
		// Incomplete when the bytes end inside a field or an open sequence.
		// Malformed input throws SofabException (Invalid) from Feed. msg always
		// receives the fields decoded so far; on Incomplete the caller owns
		// end-of-input and decides whether truncation is an error.
		f.line("    public static DecodeStatus TryDecode(byte[] data, out %s msg) {", name)
		f.line("        msg = new %s();", name)
		f.line("        return new IStream().Feed(data, 0, data.Length, new %sVisitor(msg));", name)
		f.line("    }")
	}
	f.line("}")
	f.blank()

	if isMessage {
		// The flat-visitor decode writes into reachable fields, including any
		// marked [Obsolete] (message-level or nested struct/union); silence the
		// CS0618 deprecation warning around the generated decode.
		depVis := framesHaveDeprecated(g.frames(&ir.Message{Name: name, Fields: fields}))
		if depVis {
			f.line("#pragma warning disable 618 // internal access to a member marked [Obsolete]")
		}
		g.emitVisitor(f, name, fields)
		if depVis {
			f.line("#pragma warning restore 618")
			f.blank()
		}
	}
}

// hasDeprecatedDirect reports whether any direct field of a class is deprecated,
// so its Marshal reads a member marked [Obsolete] and needs the CS0618 guard.
func hasDeprecatedDirect(fields []*ir.Field) bool {
	for _, fld := range fields {
		if fld.Deprecated {
			return true
		}
	}
	return false
}

// framesHaveDeprecated reports whether any object-scope field reachable from a
// message (message-level or a nested struct/union) is deprecated, so the
// generated visitor writes into a member marked [Obsolete].
func framesHaveDeprecated(fs []frame) bool {
	for _, fr := range fs {
		if fr.isArr {
			continue
		}
		for _, fld := range fr.fields {
			if fld.Deprecated {
				return true
			}
		}
	}
	return false
}

func (g *gen) emitMarshal(f *cfile, fld *ir.Field) {
	acc := "this." + csIdent(fld.Name)
	var write string
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		write = fmt.Sprintf("os.WriteUnsigned(%d, (ulong)%s);", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		write = fmt.Sprintf("os.WriteSigned(%d, (long)%s);", fld.ID, acc)
	case ir.KindBool:
		write = fmt.Sprintf("os.WriteBoolean(%d, %s);", fld.ID, acc)
	case ir.KindFP32:
		write = fmt.Sprintf("os.WriteFp32(%d, %s);", fld.ID, acc)
	case ir.KindFP64:
		write = fmt.Sprintf("os.WriteFp64(%d, %s);", fld.ID, acc)
	case ir.KindString:
		write = fmt.Sprintf("os.WriteString(%d, %s ?? \"\");", fld.ID, acc)
	case ir.KindBlob:
		// A blob is a leaf: omit when equal to its default (empty if none). The
		// decoder reconstructs the omitted field from its materialized default.
		// With an empty default the content compare degenerates to a length
		// check, sparing the LINQ SequenceEqual enumeration per call.
		if g.csDefaultValue(fld) == "Array.Empty<byte>()" {
			f.line("        if (%s != null && %s.Length != 0) { os.WriteBlob(%d, %s); }", acc, acc, fld.ID, acc)
			return
		}
		f.line("        if (!System.Linq.Enumerable.SequenceEqual(%s ?? Array.Empty<byte>(), %s)) { os.WriteBlob(%d, %s ?? Array.Empty<byte>()); }", acc, g.csDefaultValue(fld), fld.ID, acc)
		return
	case ir.KindStruct, ir.KindUnion:
		// A sequence is always framed; its child fields are omitted per-field by
		// the nested Marshal (MESSAGE_SPEC S2). An all-default nested object thus
		// becomes an empty wrapper sequence, not a dropped field.
		f.line("        os.WriteSequenceBegin(%d); (%s ?? new %s()).Marshal(os); os.WriteSequenceEnd();", fld.ID, acc, g.typeName(fld.Ref.Key))
		return
	case ir.KindArray:
		g.emitMarshalArray(f, fld, acc)
		return
	}
	// Scalar/string/enum/bitfield leaf: always omit when equal to the default;
	// sparse encoding is canonical (MESSAGE_SPEC S2) and the decoder reconstructs
	// the omitted field from its default.
	f.line("        if (%s != %s) { %s }", acc, g.csDefaultValue(fld), write)
}

func (g *gen) emitMarshalArray(f *cfile, fld *ir.Field, acc string) {
	// A native scalar array is a leaf field: omit it when equal to its default
	// (materialized in the field initializer), else when empty. A composite/
	// dynamic-element array is a wrapper sequence and is always framed.
	if primArrayElem(fld.Elem) {
		// Primitive array (T[]): written straight to the OStream overload with
		// no List.ToArray temporary.
		if def, ok := g.csPrimArrayLiteral(fld); ok {
			f.line("        if (!System.Linq.Enumerable.SequenceEqual(%s, %s)) {", acc, def)
		} else {
			f.line("        if (%s != null && %s.Length != 0) {", acc, acc)
		}
		g.marshalArray(f, "            ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, true)
		f.line("        }")
		return
	}
	if nativeArrayElem(fld.Elem) {
		if def, ok := g.csNativeArrayLiteral(fld); ok {
			f.line("        if (!System.Linq.Enumerable.SequenceEqual(%s, %s)) {", acc, def)
		} else {
			f.line("        if (%s.Count != 0) {", acc)
		}
		g.marshalArray(f, "            ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, false)
		f.line("        }")
		return
	}
	g.marshalArray(f, "        ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, false)
}

// marshalArray writes the list `val` as field `idExpr`. Numeric/enum/boolean/
// bitfield elements use the native array wire type (enum -> signed, boolean/
// bitfield -> unsigned, value-converted to the corelib's array element width);
// string/blob/struct/union/array elements lower to a wrapper sequence whose child
// ids are the 0-based index (per MESSAGE_SPEC). Recurses for nested arrays,
// depth-suffixing the loop var to avoid collisions. isPrim marks `val` as a
// primitive T[] field (written directly); inner rows of nested arrays stay
// List<T> and bridge via ToArray.
func (g *gen) marshalArray(f *cfile, ind, idExpr, val string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int, isPrim bool) {
	iv := fmt.Sprintf("_i%d", depth)
	arr := val + ".ToArray()"
	if isPrim {
		arr = val
	}
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64:
		f.line("%sos.WriteArrayUnsigned(%s, %s);", ind, idExpr, arr)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
		f.line("%sos.WriteArraySigned(%s, %s);", ind, idExpr, arr)
	case ir.KindFP32:
		f.line("%sos.WriteArrayFp32(%s, %s);", ind, idExpr, arr)
	case ir.KindFP64:
		f.line("%sos.WriteArrayFp64(%s, %s);", ind, idExpr, arr)
	case ir.KindEnum:
		// enum -> signed array at the enum's backing width (matches Go's typed array).
		f.line("%sos.WriteArraySigned(%s, Array.ConvertAll(%s.ToArray(), _x => (%s)_x));", ind, idExpr, val, enumBacking(ref.Target))
	case ir.KindBitfield:
		f.line("%sos.WriteArrayUnsigned(%s, Array.ConvertAll(%s.ToArray(), _x => (%s)_x));", ind, idExpr, val, bitfieldBacking(ref.Target))
	case ir.KindBool:
		// boolean -> unsigned u8 array of 0/1.
		f.line("%sos.WriteArrayUnsigned(%s, Array.ConvertAll(%s.ToArray(), _x => _x ? (byte)1 : (byte)0));", ind, idExpr, val)
	case ir.KindString:
		// A string element is a leaf: omit it when equal to the element default
		// (empty), leaving an id gap the decoder restores (MESSAGE_SPEC S2).
		f.line("%sos.WriteSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.Count; %s++) { if ((%s[%s] ?? \"\") != \"\") os.WriteString(%s, %s[%s] ?? \"\"); }", ind, iv, iv, val, iv, val, iv, iv, val, iv)
		f.line("%sos.WriteSequenceEnd();", ind)
	case ir.KindBlob:
		// A blob element is a leaf: omit it when equal to the element default
		// (empty), leaving an id gap the decoder restores (MESSAGE_SPEC S2).
		f.line("%sos.WriteSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.Count; %s++) { if ((%s[%s] ?? Array.Empty<byte>()).Length != 0) os.WriteBlob(%s, %s[%s] ?? Array.Empty<byte>()); }", ind, iv, iv, val, iv, val, iv, iv, val, iv)
		f.line("%sos.WriteSequenceEnd();", ind)
	case ir.KindStruct, ir.KindUnion:
		f.line("%sos.WriteSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.Count; %s++) {", ind, iv, iv, val, iv)
		f.line("%s    os.WriteSequenceBegin(%s); (%s[%s] ?? new %s()).Marshal(os); os.WriteSequenceEnd();", ind, iv, val, iv, g.typeName(ref.Key))
		f.line("%s}", ind)
		f.line("%sos.WriteSequenceEnd();", ind)
	case ir.KindArray:
		f.line("%sos.WriteSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.Count; %s++) {", ind, iv, iv, val, iv)
		g.marshalArray(f, ind+"    ", iv, fmt.Sprintf("%s[%s]", val, iv), items.Elem, items.ElemRef, items.ElemItems, depth+1, false)
		f.line("%s}", ind)
		f.line("%sos.WriteSequenceEnd();", ind)
	}
}
