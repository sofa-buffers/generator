// projectFiles scaffolds a buildable .NET console project with a
// System.Text.Json encode/decode harness over corelib-cs.
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	return []generator.File{
		{Path: "harness.csproj", Content: []byte(csproj)},
		{Path: "Program.cs", Content: g.harness(s)},
		{Path: "README.md", Content: []byte(csReadme)},
	}
}

const csReadme = "# Generated SofaBuffers C# project\n\n" +
	"`@generated by sofabgen`. Encode/decode harness over `corelib-cs`.\n\n" +
	"```sh\nSOFAB_CS_CORELIB=/path/to/corelib-cs dotnet run -- encode <Message> < in.json\n```\n"

