// projectFiles scaffolds a runnable TS project: an encode/decode JSON harness
// over the generated module, a package.json depending on @sofa-buffers/corelib,
// and a tsconfig (PLAN §9.1). Run with `npx tsx harness.ts`.
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	return []generator.File{
		{Path: "harness.ts", Content: g.harness(s)},
		{Path: "package.json", Content: []byte(packageJSON)},
		{Path: "tsconfig.json", Content: []byte(tsconfig)},
		{Path: "README.md", Content: []byte(readme)},
	}
}

const packageJSON = `{
  "//": "@generated by sofabgen",
  "name": "sofabuffers-generated",
  "private": true,
  "type": "module",
  "dependencies": { "@sofa-buffers/corelib": "*" },
  "devDependencies": { "tsx": "^4", "typescript": "^5", "@types/node": "^20" }
}
`

const tsconfig = `{
  "compilerOptions": {
    "target": "ES2020",
    "module": "ESNext",
    "moduleResolution": "bundler",
    "strict": true,
    "skipLibCheck": true,
    "noEmit": true
  }
}
`

const readme = "# Generated SofaBuffers TypeScript module\n\n" +
	"`@generated by sofabgen`. Encode/decode harness over `@sofa-buffers/corelib`.\n\n" +
	"```sh\nnpm install\n" +
	"echo '{...}' | npx tsx harness.ts encode <Message>   # JSON -> bytes\n" +
	"npx tsx harness.ts decode <Message> < bytes.bin       # bytes -> JSON\n```\n"

func (g *gen) harness(s *ir.Schema) []byte {
	f := &tsfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	f.line("import * as M from \"./messages.js\";")
	f.blank()
	f.line("const MESSAGES: Record<string, { fromJSON(d: Record<string, unknown>): { marshal(os: import(%q).OStream): void; toJSON(): Record<string, unknown> }; decode(b: Uint8Array): { toJSON(): Record<string, unknown> } }> = {", corelibPkg)
	for _, m := range s.Messages {
		f.line("  %q: M.%s,", m.Name, exported(m.Name))
	}
	f.line("};")
	f.blank()
	f.line("async function main(): Promise<number> {")
	f.line("  const mode = process.argv[2];")
	f.line("  const name = process.argv[3] ?? %q;", defaultMessage(s))
	f.line("  const cls = MESSAGES[name];")
	f.line("  if (!cls) { process.stderr.write(\"unknown message\\n\"); return 2; }")
	f.line("  const chunks: Uint8Array[] = [];")
	f.line("  for await (const c of process.stdin) chunks.push(c as Uint8Array);")
	f.line("  const input = Buffer.concat(chunks);")
	f.line("  if (mode === \"encode\") {")
	f.line("    const obj = cls.fromJSON(JSON.parse(input.toString(\"utf8\")));")
	f.line("    const os = new (await import(%q)).OStream();", corelibPkg)
	f.line("    obj.marshal(os);")
	f.line("    process.stdout.write(os.bytes());")
	f.line("  } else if (mode === \"decode\") {")
	f.line("    const obj = cls.decode(new Uint8Array(input));")
	f.line("    process.stdout.write(JSON.stringify(obj.toJSON()) + \"\\n\");")
	f.line("  } else { process.stderr.write(\"unknown mode\\n\"); return 2; }")
	f.line("  return 0;")
	f.line("}")
	f.blank()
	f.line("main().then((c) => process.exit(c));")
	return f.bytes()
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}

