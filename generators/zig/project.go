// emitBench emits the Callgrind workload entry points (tests/bench, ARCHITECTURE
// §15). Each run_<workload> performs EXACTLY ONE op and is `export fn` (unmangled,
// external) + @call(.never_inline), so
//
//	valgrind --tool=callgrind --collect-atstart=no --toggle-collect=run_<workload>
//
// collects that single op and nothing else — process start, JSON parsing and the
// decode input's pre-encode all happen in benchMain, before collection is toggled
// on. Same shape as corelib-zig/bench.
//
// The never_inline barrier is on the wrapper only, so encode/decode and the corelib
// still inline into it — that inlining is the cost being measured. std.mem.
// doNotOptimizeAway keeps the results live.
func (g *gen) emitBench(f *zfile, s *ir.Schema) {
	for _, m := range s.Messages {
		mt := exported(m.Name)
		low := strings.ToLower(m.Name)
		f.line("var _bench_%s_in: message.%s = undefined;", low, mt)
		f.line("var _bench_%s_wire: []const u8 = &[_]u8{};", low)
		f.line("var _bench_%s_alloc: std.mem.Allocator = undefined;", low)
		f.blank()
		f.line("export fn run_encode_%s() void {", low)
		f.line("    _bench_%s_wire = _bench_%s_in.encode(_bench_%s_alloc) catch &[_]u8{};", low, low, low)
		f.line("    std.mem.doNotOptimizeAway(&_bench_%s_wire);", low)
		f.line("}")
		f.blank()
		f.line("export fn run_decode_%s() void {", low)
		f.line("    const obj = message.%s.decode(_bench_%s_alloc, _bench_%s_wire) catch return;", mt, low, low)
		f.line("    std.mem.doNotOptimizeAway(&obj);")
		f.line("}")
		f.blank()
	}

	f.line("// benchMain runs one op of <workload>. Everything here is setup; only the")
	f.line("// run_* call is collected.")
	f.line("fn benchMain(alloc: std.mem.Allocator, w: []const u8, input: []const u8) !void {")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		low := strings.ToLower(m.Name)
		f.line("    if (std.mem.eql(u8, w, \"encode_%s\") or std.mem.eql(u8, w, \"decode_%s\")) {", low, low)
		f.line("        const v = try std.json.parseFromSliceLeaky(std.json.Value, alloc, input, .{});")
		f.line("        _bench_%s_alloc = alloc;", low)
		f.line("        _bench_%s_in = fromJson_%s(alloc, v);", low, mt)
		// @call(.never_inline) at the CALL SITE, not just `export fn`: ReleaseFast
		// happily inlines the body into benchMain and leaves the exported symbol as
		// an unreferenced copy, so --toggle-collect matches a function that is never
		// entered and callgrind silently reports Ir = 0.
		f.line("        if (std.mem.eql(u8, w, \"encode_%s\")) {", low)
		f.line("            @call(.never_inline, run_encode_%s, .{});", low)
		f.line("        } else {")
		f.line("            @call(.never_inline, run_encode_%s, .{}); // setup: the decode input (not collected)", low)
		f.line("            @call(.never_inline, run_decode_%s, .{});", low)
		f.line("        }")
		f.line("        std.debug.print(\"BYTES={d}\\n\", .{_bench_%s_wire.len});", low)
		f.line("        return;")
		f.line("    }")
	}
	f.line("    std.process.exit(2);")
	f.line("}")
	f.blank()
}

// harness emits src/main.zig: a stdin/stdout JSON encode/decode driver with
// hand-rolled JSON mapping per generated type. std.json's dynamic Value is
// used for parsing (its number_string form keeps u64 values above 2^53
// exact); output is written directly so u64/i64 print at full precision.
func (g *gen) harness(s *ir.Schema) []byte {
	f := &zfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("// SPDX-License-Identifier: %s", g.license)
	}
	f.line("// JSON encode/decode harness: harness encode|decode <message> (stdin -> stdout).")
	f.line("const std = @import(\"std\");")
	f.line("const message = @import(\"message.zig\");")
	f.blank()
	f.line("%s", jsonHelpers)

	// Per-type JSON converters (shared named types first, then messages).
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitJSON(f, g.typeName(key), nt.Fields)
		}
	}
	for _, m := range s.Messages {
		g.emitJSON(f, exported(m.Name), m.Fields)
	}

	g.emitBench(f, s)
	f.line("pub fn main(init: std.process.Init) !void {")
	f.line("    const alloc = init.arena.allocator();")
	f.line("    var args = std.process.Args.Iterator.init(init.minimal.args);")
	f.line("    _ = args.next();")
	f.line("    const mode = args.next() orelse \"\";")
	f.line("    const name = args.next() orelse %q;", defaultMessage(s))
	f.blank()
	f.line("    var rbuf: [4096]u8 = undefined;")
	f.line("    var stdin = std.Io.File.stdin().reader(init.io, &rbuf);")
	f.line("    const input = try stdin.interface.allocRemaining(alloc, .unlimited);")
	f.line("    var wbuf: [4096]u8 = undefined;")
	f.line("    var stdout = std.Io.File.stdout().writer(init.io, &wbuf);")
	f.line("    const out = &stdout.interface;")
	f.blank()
	f.line("    // `bench <workload>` takes a workload, not a message name.")
	f.line("    if (std.mem.eql(u8, mode, \"bench\")) {")
	f.line("        try benchMain(alloc, name, input);")
	f.line("        return;")
	f.line("    }")
	f.blank()
	for _, m := range s.Messages {
		mt := exported(m.Name)
		f.line("    if (std.mem.eql(u8, name, %q)) {", m.Name)
		f.line("        if (std.mem.eql(u8, mode, \"encode\")) {")
		f.line("            const v = try std.json.parseFromSliceLeaky(std.json.Value, alloc, input, .{});")
		f.line("            const obj = fromJson_%s(alloc, v);", mt)
		f.line("            try out.writeAll(try obj.encode(alloc));")
		f.line("        } else if (std.mem.eql(u8, mode, \"decode\")) {")
		f.line("            const obj = try message.%s.decode(alloc, input);", mt)
		f.line("            try toJson_%s(&obj, out);", mt)
		f.line("            try out.writeByte('\\n');")
		f.line("        } else {")
		f.line("            std.process.exit(2);")
		f.line("        }")
		f.line("        try out.flush();")
		f.line("        return;")
		f.line("    }")
	}
	f.line("    std.process.exit(2);")
	f.line("}")
	return f.bytes()
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}

const jsonHelpers = `// -- JSON value readers (tolerant: a missing/mistyped member keeps the default)
fn jsonU64(v: std.json.Value) u64 {
    switch (v) {
        .integer => |i| return if (i < 0) 0 else @intCast(i),
        .float => |f| return if (f < 0) 0 else @intFromFloat(f),
        .number_string => |s| return std.fmt.parseInt(u64, s, 10) catch 0,
        else => return 0,
    }
}

fn jsonI64(v: std.json.Value) i64 {
    switch (v) {
        .integer => |i| return i,
        .float => |f| return @intFromFloat(f),
        .number_string => |s| return std.fmt.parseInt(i64, s, 10) catch 0,
        else => return 0,
    }
}

fn jsonF64(v: std.json.Value) f64 {
    switch (v) {
        .integer => |i| return @floatFromInt(i),
        .float => |f| return f,
        .number_string => |s| return std.fmt.parseFloat(f64, s) catch 0,
        else => return 0,
    }
}

fn jsonBool(v: std.json.Value) bool {
    return switch (v) {
        .bool => |b| b,
        else => false,
    };
}

fn jsonStr(v: std.json.Value) []const u8 {
    return switch (v) {
        .string => |s| s,
        else => "",
    };
}

// A blob is a JSON array of byte values.
fn jsonBlob(alloc: std.mem.Allocator, v: std.json.Value) []const u8 {
    switch (v) {
        .array => |a| {
            const s = alloc.alloc(u8, a.items.len) catch @panic("oom");
            for (a.items, 0..) |it, k| s[k] = @truncate(jsonU64(it));
            return s;
        },
        else => return "",
    }
}

fn jsonWriteBytes(b: []const u8, w: *std.Io.Writer) !void {
    try w.writeByte('[');
    for (b, 0..) |e, i| {
        if (i != 0) try w.writeByte(',');
        try w.print("{d}", .{e});
    }
    try w.writeByte(']');
}
`

// emitJSON writes toJson_<T> / fromJson_<T> for one generated struct type.
func (g *gen) emitJSON(f *zfile, name string, fields []*ir.Field) {
	// toJson: every field, schema order, schema names.
	f.line("fn toJson_%s(o: *const message.%s, w: *std.Io.Writer) std.Io.Writer.Error!void {", name, name)
	f.line("    try w.writeByte('{');")
	for i, fld := range fields {
		comma := ","
		if i == 0 {
			comma = ""
		}
		f.line("    try w.writeAll(\"%s\\\"%s\\\":\");", comma, fld.Name)
		acc := "o." + zigIdent(fld.Name)
		if fld.Kind == ir.KindArray {
			ed := arrayElemOf(fld)
			g.emitToJSONValue(f, "    ", acc, ir.ArrayElem{Elem: ir.KindArray, ElemItems: &ed}, 0)
		} else {
			g.emitToJSONValue(f, "    ", acc, fieldElem(fld), 0)
		}
	}
	f.line("    try w.writeByte('}');")
	f.line("}")
	f.blank()

	// fromJson: tolerant member-by-member load over std.json.Value. Zig
	// rejects unused AND pointlessly-discarded names alike, so the discards
	// are emitted exactly when a name goes unused.
	f.line("fn fromJson_%s(alloc: std.mem.Allocator, v: std.json.Value) message.%s {", name, name)
	f.line("    var o: message.%s = .{};", name)
	f.line("    const obj = switch (v) {")
	f.line("        .object => |ob| ob,")
	f.line("        else => return o,")
	f.line("    };")
	if !fieldsUseAlloc(fields) {
		f.line("    _ = alloc;")
	}
	if len(fields) == 0 {
		f.line("    _ = obj;")
	}
	for _, fld := range fields {
		g.emitFromJSONField(f, fld)
	}
	f.line("    return o;")
	f.line("}")
	f.blank()
}

// fieldElem views a field as an array-element descriptor so scalar and
// element emission share one code path.
func fieldElem(f *ir.Field) ir.ArrayElem {
	return ir.ArrayElem{
		Elem: f.Kind, ElemRef: f.Ref, ElemItems: nil,
	}
}

// emitToJSONValue writes one JSON value for `acc` of the given element kind.
func (g *gen) emitToJSONValue(f *zfile, ind, acc string, e ir.ArrayElem, depth int) {
	switch e.Elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64,
		ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64,
		ir.KindFP32, ir.KindFP64, ir.KindEnum, ir.KindBitfield:
		f.line("%stry w.print(\"{d}\", .{%s});", ind, acc)
	case ir.KindBool:
		f.line("%stry w.writeAll(if (%s) \"true\" else \"false\");", ind, acc)
	case ir.KindString:
		f.line("%stry std.json.Stringify.encodeJsonString(%s, .{}, w);", ind, acc)
	case ir.KindBlob:
		f.line("%stry jsonWriteBytes(%s, w);", ind, acc)
	case ir.KindStruct, ir.KindUnion:
		f.line("%stry toJson_%s(&%s, w);", ind, g.typeName(e.ElemRef.Key), acc)
	case ir.KindArray:
		// e.ElemItems describes the element; recurse per element.
		ev := fmt.Sprintf("_e%d", depth)
		iv := fmt.Sprintf("_i%d", depth)
		f.line("%stry w.writeByte('[');", ind)
		f.line("%sfor (%s, 0..) |%s, %s| {", ind, acc, ev, iv)
		f.line("%s    if (%s != 0) try w.writeByte(',');", ind, iv)
		g.emitToJSONValue(f, ind+"    ", ev, *e.ElemItems, depth+1)
		f.line("%s}", ind)
		f.line("%stry w.writeByte(']');", ind)
	}
}

func (g *gen) emitFromJSONField(f *zfile, fld *ir.Field) {
	get := fmt.Sprintf("obj.get(%q)", fld.Name)
	acc := "o." + zigIdent(fld.Name)
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		dest := numZigType(fld.Kind)
		if fld.Kind == ir.KindBitfield {
			dest = bitfieldBacking(fld.Ref.Target)
		}
		f.line("    if (%s) |x| %s = %s;", get, acc, storeCast(dest, "jsonU64(x)"))
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		dest := numZigType(fld.Kind)
		if fld.Kind == ir.KindEnum {
			dest = enumBacking(fld.Ref.Target)
		}
		f.line("    if (%s) |x| %s = %s;", get, acc, storeCast(dest, "jsonI64(x)"))
	case ir.KindFP32:
		f.line("    if (%s) |x| %s = @floatCast(jsonF64(x));", get, acc)
	case ir.KindFP64:
		f.line("    if (%s) |x| %s = jsonF64(x);", get, acc)
	case ir.KindBool:
		f.line("    if (%s) |x| %s = jsonBool(x);", get, acc)
	case ir.KindString:
		f.line("    if (%s) |x| %s = jsonStr(x);", get, acc)
	case ir.KindBlob:
		f.line("    if (%s) |x| %s = jsonBlob(alloc, x);", get, acc)
	case ir.KindStruct, ir.KindUnion:
		f.line("    if (%s) |x| %s = fromJson_%s(alloc, x);", get, acc, g.typeName(fld.Ref.Key))
	case ir.KindArray:
		f.line("    if (%s) |x| switch (x) {", get)
		f.line("        .array => |a0| {")
		if _, n, ok := g.fixedNativeArray(fld); ok {
			f.line("            for (a0.items, 0..) |it0, k0| {")
			f.line("                if (k0 >= %d) break;", n)
			f.line("                %s[k0] = %s;", acc, g.fromJSONElemExpr(arrayElemOf(fld), 0))
			f.line("            }")
		} else {
			elemType := g.harnessElemType(arrayElemOf(fld))
			f.line("            const s0 = alloc.alloc(%s, a0.items.len) catch @panic(\"oom\");", elemType)
			f.line("            for (a0.items, 0..) |it0, k0| s0[k0] = %s;", g.fromJSONElemExpr(arrayElemOf(fld), 0))
			f.line("            %s = s0;", acc)
		}
		f.line("        },")
		f.line("        else => {},")
		f.line("    };")
	}
}

// arrayElemOf views a field's items as an ArrayElem.
func arrayElemOf(f *ir.Field) ir.ArrayElem {
	return ir.ArrayElem{Elem: f.Elem, ElemRef: f.ElemRef, ElemItems: f.ElemItems}
}

// harnessElemType is the element Zig type as seen from the harness module:
// generated struct/union type names live in the imported `message` namespace.
func (g *gen) harnessElemType(e ir.ArrayElem) string {
	switch e.Elem {
	case ir.KindStruct, ir.KindUnion:
		return "message." + g.typeName(e.ElemRef.Key)
	case ir.KindArray:
		return "[]const " + g.harnessElemType(*e.ElemItems)
	}
	return g.zigArrayElem(e.Elem, e.ElemRef, e.ElemItems)
}

// fieldsUseAlloc reports whether fromJson for these fields references its
// allocator: blobs and struct/union loads always do, arrays do unless they
// lower to a fixed [N]T destination.
func fieldsUseAlloc(fields []*ir.Field) bool {
	for _, f := range fields {
		switch f.Kind {
		case ir.KindBlob, ir.KindStruct, ir.KindUnion:
			return true
		case ir.KindArray:
			// Fixed native arrays fill in place; anything else allocates.
			if !(isNativeArrayElem(f.Elem) && f.HasCount) {
				return true
			}
		}
	}
	return false
}

// fromJSONElemExpr renders the expression converting JSON value `it<depth>`
// into one array element, recursing (via a block expression) for nested
// arrays.
func (g *gen) fromJSONElemExpr(e ir.ArrayElem, depth int) string {
	it := fmt.Sprintf("it%d", depth)
	switch e.Elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64:
		return storeCast(numZigType(e.Elem), fmt.Sprintf("jsonU64(%s)", it))
	case ir.KindBitfield:
		return storeCast(bitfieldBacking(e.ElemRef.Target), fmt.Sprintf("jsonU64(%s)", it))
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
		return storeCast(numZigType(e.Elem), fmt.Sprintf("jsonI64(%s)", it))
	case ir.KindEnum:
		return storeCast(enumBacking(e.ElemRef.Target), fmt.Sprintf("jsonI64(%s)", it))
	case ir.KindFP32:
		return fmt.Sprintf("@floatCast(jsonF64(%s))", it)
	case ir.KindFP64:
		return fmt.Sprintf("jsonF64(%s)", it)
	case ir.KindBool:
		return fmt.Sprintf("jsonBool(%s)", it)
	case ir.KindString:
		return fmt.Sprintf("jsonStr(%s)", it)
	case ir.KindBlob:
		return fmt.Sprintf("jsonBlob(alloc, %s)", it)
	case ir.KindStruct, ir.KindUnion:
		return fmt.Sprintf("fromJson_%s(alloc, %s)", g.typeName(e.ElemRef.Key), it)
	case ir.KindArray:
		inner := e.ElemItems
		innerType := g.harnessElemType(*inner)
		d := depth + 1
		return fmt.Sprintf("switch (%s) { .array => |a%d| blk%d: { "+
			"const s%d = alloc.alloc(%s, a%d.items.len) catch @panic(\"oom\"); "+
			"for (a%d.items, 0..) |it%d, k%d| s%d[k%d] = %s; "+
			"break :blk%d s%d; }, else => &.{} }",
			it, d, d, d, innerType, d, d, d, d, d, d,
			g.fromJSONElemExpr(*inner, d), d, d)
	}
	return "undefined"
}
