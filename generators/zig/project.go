// projectFiles scaffolds a buildable Zig package over corelib-zig: build.zig +
// build.zig.zon (with a ${SOFAB_ZIG_CORELIB} path placeholder) and a JSON
// encode/decode harness (src/main.zig) driving the generated types.
func (g *gen) projectFiles(s *ir.Schema) []generator.File {
	return []generator.File{
		{Path: "build.zig", Content: []byte(zigBuild)},
		{Path: "build.zig.zon", Content: []byte(zigZon)},
		{Path: "src/main.zig", Content: g.harness(s)},
		{Path: "README.md", Content: []byte(zigReadme)},
	}
}

const zigBuild = `// @generated by sofabgen
const std = @import("std");

pub fn build(b: *std.Build) void {
    const target = b.standardTargetOptions(.{});
    // corelib-zig is the max-speed port: --release resolves to ReleaseFast,
    // the configuration the corelib is tuned for. Plain zig build = Debug.
    const optimize = b.standardOptimizeOption(.{ .preferred_optimize_mode = .ReleaseFast });

    // The corelib's own build resolves ReleaseFast via --release (it uses
    // standardOptimizeOption with a preferred mode, not -Doptimize), so only
    // the target is forwarded.
    const sofab = b.dependency("sofa_buffers_corelib", .{
        .target = target,
    }).module("sofab");

    const mod = b.createModule(.{
        .root_source_file = b.path("src/main.zig"),
        .target = target,
        .optimize = optimize,
    });
    mod.addImport("sofab", sofab);

    const exe = b.addExecutable(.{ .name = "harness", .root_module = mod });
    b.installArtifact(exe);
}
`

