// Package python is the Python throughput backend (PLAN §6.4): it emits
// dataclasses with _marshal (Encoder) and a pull-parser _unmarshal
// (Decoder.next/skip + typed readers) against corelib-py, plus JSON helpers for
// the conformance harness.
package python

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

func init() { generator.Register(&Backend{}) }

// Backend implements generator.Backend for Python.
type Backend struct{}

func (*Backend) Lang() string { return "python" }

// Generate emits a single module (all enums/bitfields/dataclasses + messages).
// In project mode it adds a harness and pyproject.toml.
func (*Backend) Generate(s *ir.Schema, cfg map[string]any) ([]generator.File, error) {
	g := &gen{
		schema:  s,
		banner:  cfgString(cfg, "tool_banner", "sofabgen"),
		license: generator.LicenseID(cfg),
		limits:  resolveLimits(s, cfg),
	}
	module := g.module(s)
	files := []generator.File{{Path: "message.py", Content: module}}
	if cfgString(cfg, "emit", "sources") == "project" {
		files = append(files, g.projectFiles(s, cfg)...)
	}
	return files, nil
}

type gen struct {
	schema  *ir.Schema
	banner  string
	license string // SPDX id, "" to omit the header line
	limits  limitSet
}

// limitSet is the receiver-side decode-limit configuration (generator#102),
// resolved against the schema: each active entry is the configured cap raised
// to the largest schema bound of its kind, so a schema-bounded field larger
// than the cap stays governed by its schema bound alone (corelib-py enforces
// these globally per Decoder). An entry is active only when its key is
// configured AND the schema actually has an unbounded field of that kind --
// otherwise the option would be inert and no plumbing is emitted.
type limitSet struct {
	arrayCount, stringLen, blobLen int64
	arrayHas, stringHas, blobHas   bool
}

func (l limitSet) any() bool { return l.arrayHas || l.stringHas || l.blobHas }

// resolveLimits reads the max_dyn_* config keys and resolves them against the
// schema's bounds (see limitSet).
func resolveLimits(s *ir.Schema, cfg map[string]any) limitSet {
	var all []*ir.Field
	for _, m := range s.Messages {
		all = append(all, m.Fields...)
	}
	b := ir.Bounds(all)
	var l limitSet
	if v, ok := cfgLimit(cfg, "max_dyn_array_count"); ok && b.HasDynArray {
		l.arrayCount, l.arrayHas = max(v, b.MaxCount), true
	}
	if v, ok := cfgLimit(cfg, "max_dyn_string_len"); ok && b.HasDynString {
		l.stringLen, l.stringHas = max(v, b.MaxStringLen), true
	}
	if v, ok := cfgLimit(cfg, "max_dyn_blob_len"); ok && b.HasDynBlob {
		l.blobLen, l.blobHas = max(v, b.MaxBlobLen), true
	}
	return l
}

type pyfile struct{ b strings.Builder }

func (f *pyfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}
func (f *pyfile) blank()        { f.b.WriteByte('\n') }
func (f *pyfile) bytes() []byte { return []byte(f.b.String()) }

func (g *gen) module(s *ir.Schema) []byte {
	f := &pyfile{}
	f.line("# Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("# SPDX-License-Identifier: %s", g.license)
	}
	f.line("from __future__ import annotations")
	f.line("import io")
	f.line("from dataclasses import dataclass, field")
	f.line("from enum import IntEnum")
	// SofaDecodeError is raised by the over-count scalar-array guard
	// (generator#100) and the over-index wrapper-array guard (generator#142), so
	// import it only when a fixed-count array of either kind exists — an
	// unconditional import would be unused for other schemas.
	if schemaHasCountedFloatArray(s) {
		f.line("import math")
	}
	names := []string{"Encoder", "Decoder"}
	if schemaHasCountedNativeArray(s) || schemaHasCountedWrapperArray(s) || schemaHasMaxlenStringBlob(s) {
		names = append(names, "SofaDecodeError")
	}
	names = append(names, "WireType")
	// FixlenSubtype is referenced only by the §7.3 guard of a fixlen-framed field;
	// importing it unconditionally would leave it unused for schemas without one.
	if schemaHasFixlenField(s) {
		names = append(names, "FixlenSubtype")
	}
	f.line("from sofab import %s", strings.Join(names, ", "))
	f.blank()

	g.emitFixedArrayHelpers(f, s)

	if g.limits.any() {
		f.line("# Receiver-side decode limits, baked from the sofabgen config")
		f.line("# (max_dyn_array_count / max_dyn_string_len / max_dyn_blob_len). They govern")
		f.line("# only fields the schema left unbounded; each cap is raised to the largest")
		f.line("# schema bound of its kind, so a schema-bounded field stays governed by its")
		f.line("# own bound alone. Exceeding a cap raises sofab.SofaLimitError at the count/")
		f.line("# length header, before any allocation.")
		if g.limits.arrayHas {
			f.line("MAX_DYN_ARRAY_COUNT = %d", g.limits.arrayCount)
		}
		if g.limits.stringHas {
			f.line("MAX_DYN_STRING_LEN = %d", g.limits.stringLen)
		}
		if g.limits.blobHas {
			f.line("MAX_DYN_BLOB_LEN = %d", g.limits.blobLen)
		}
		f.blank()
	}

	// enums + bitfield constants first
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		switch nt.Category {
		case ir.CatEnum:
			g.emitEnum(f, nt)
		case ir.CatBitfield:
			g.emitBitfieldConsts(f, nt)
		}
	}
	// struct/union dataclasses, then messages
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitDataclass(f, g.typeName(key), nt.Summary, nt.Fields)
		}
	}
	for _, m := range s.Messages {
		g.emitDataclass(f, exported(m.Name), m.Summary, m.Fields)
	}
	return f.bytes()
}

// schemaHasCountedNativeArray reports whether any message or named-type field
// is a native scalar array with a schema `count` — the fields whose decode
// emits the over-count SofaDecodeError guard (generator#100).
func schemaHasCountedNativeArray(s *ir.Schema) bool {
	counted := func(fields []*ir.Field) bool {
		for _, fld := range fields {
			if fld.Kind == ir.KindArray && fld.HasCount && isNativeArrayElem(fld.Elem) {
				return true
			}
		}
		return false
	}
	for _, key := range s.NamedOrder {
		if counted(s.Named[key].Fields) {
			return true
		}
	}
	for _, m := range s.Messages {
		if counted(m.Fields) {
			return true
		}
	}
	return false
}

// schemaHasCountedWrapperArray reports whether any field (recursively through
// nested-array element items) is a fixed-count wrapper-sequence array —
// string/blob/struct/union/nested-array elements with a schema `count` — the
// fields whose decode emits the over-index SofaDecodeError guard (generator#142).
func schemaHasCountedWrapperArray(s *ir.Schema) bool {
	var arrHas func(elem ir.Kind, items *ir.ArrayElem, hasCount bool) bool
	arrHas = func(elem ir.Kind, items *ir.ArrayElem, hasCount bool) bool {
		if hasCount && !isNativeArrayElem(elem) {
			return true
		}
		if elem == ir.KindArray && items != nil {
			return arrHas(items.Elem, items.ElemItems, items.HasCount)
		}
		return false
	}
	return schemaHasField(s, func(fld *ir.Field) bool {
		return fld.Kind == ir.KindArray && arrHas(fld.Elem, fld.ElemItems, fld.HasCount)
	})
}

// schemaHasMaxlenStringBlob reports whether any field (recursively through
// nested-array element items) is a bounded (maxlen) string/blob — a scalar
// string/blob carrying a schema `maxlen`, or a string/blob array element
// carrying an element maxlen — the fields whose decode emits the over-length
// SofaDecodeError guard (MESSAGE_SPEC §7.1). Kept in lockstep with the import
// condition: a schema that rejects only on a bounded string/blob still needs
// SofaDecodeError imported.
func schemaHasMaxlenStringBlob(s *ir.Schema) bool {
	var arrHas func(elem ir.Kind, items *ir.ArrayElem, elemMaxHas bool) bool
	arrHas = func(elem ir.Kind, items *ir.ArrayElem, elemMaxHas bool) bool {
		if elemMaxHas && (elem == ir.KindString || elem == ir.KindBlob) {
			return true
		}
		if elem == ir.KindArray && items != nil {
			return arrHas(items.Elem, items.ElemItems, items.ElemMaxHas)
		}
		return false
	}
	return schemaHasField(s, func(fld *ir.Field) bool {
		if (fld.Kind == ir.KindString || fld.Kind == ir.KindBlob) && fld.HasMaxlen {
			return true
		}
		return fld.Kind == ir.KindArray && arrHas(fld.Elem, fld.ElemItems, fld.ElemMaxHas)
	})
}

// schemaHasCountedFloatArray reports whether any field is a fixed-count fp32/
// fp64 array — the only case whose trim needs the bit-pattern (signed-zero)
// comparison, and so the only case that needs `import math`.
func schemaHasCountedFloatArray(s *ir.Schema) bool {
	return schemaHasField(s, func(fld *ir.Field) bool {
		return fld.Kind == ir.KindArray && fld.HasCount &&
			(fld.Elem == ir.KindFP32 || fld.Elem == ir.KindFP64)
	})
}

// schemaHasFixlenField reports whether any field's §7.3 guard needs to compare a
// fixlen subtype — i.e. whether the generated module references FixlenSubtype at
// all. fp32/fp64/string/blob and their native arrays all share one wire type
// (FIXLEN / ARRAY_FIXLEN), so only the subtype separates them; every other kind
// is settled by the wire type alone and needs no import.
func schemaHasFixlenField(s *ir.Schema) bool {
	return schemaHasField(s, func(fld *ir.Field) bool {
		if pyFixlenSubtype(fld.Kind) != "" {
			return true
		}
		return fld.Kind == ir.KindArray && isNativeArrayElem(fld.Elem) && pyFixlenSubtype(fld.Elem) != ""
	})
}

// pyFixlenSubtype returns the FixlenSubtype member a fixlen kind must carry, or
// "" for a kind that is not fixlen-framed.
func pyFixlenSubtype(k ir.Kind) string {
	switch k {
	case ir.KindFP32:
		return "FixlenSubtype.FP32"
	case ir.KindFP64:
		return "FixlenSubtype.FP64"
	case ir.KindString:
		return "FixlenSubtype.STRING"
	case ir.KindBlob:
		return "FixlenSubtype.BLOB"
	}
	return ""
}

// pyExpectedWire returns the WireType member a field's header must carry for its
// schema-typed reader to be the right one, mirroring the encode side
// (emitMarshal / marshalArray): unsigned integers, bool and bitfield ->
// UNSIGNED; signed integers and enum -> SIGNED; fp32/fp64, string and blob ->
// FIXLEN; nested messages and composite (wrapper) arrays -> SEQUENCE_START;
// native scalar arrays -> the matching ARRAY_* wire type.
func pyExpectedWire(fld *ir.Field) string {
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBool, ir.KindBitfield:
		return "WireType.UNSIGNED"
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		return "WireType.SIGNED"
	case ir.KindFP32, ir.KindFP64, ir.KindString, ir.KindBlob:
		return "WireType.FIXLEN"
	case ir.KindStruct, ir.KindUnion:
		return "WireType.SEQUENCE_START"
	case ir.KindArray:
		if !isNativeArrayElem(fld.Elem) {
			return "WireType.SEQUENCE_START"
		}
		switch fld.Elem {
		case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
			return "WireType.ARRAY_SIGNED"
		case ir.KindFP32, ir.KindFP64:
			return "WireType.ARRAY_FIXLEN"
		default: // u8/u16/u32/u64, bool, bitfield
			return "WireType.ARRAY_UNSIGNED"
		}
	}
	return "WireType.SEQUENCE_START" // unreachable: keeps the switch total
}

// pyWireGuard renders the §7.3 condition a field header must satisfy before its
// schema-typed reader may run: the wire type the declared type maps to, plus the
// fixlen subtype where the wire type alone is ambiguous (fp32/fp64/string/blob,
// and the fp32/fp64 native arrays, which share FIXLEN / ARRAY_FIXLEN).
func pyWireGuard(fld *ir.Field) string {
	cond := "fld.type != " + pyExpectedWire(fld)
	sub := pyFixlenSubtype(fld.Kind)
	if fld.Kind == ir.KindArray && isNativeArrayElem(fld.Elem) {
		sub = pyFixlenSubtype(fld.Elem)
	}
	if sub != "" {
		cond += " or fld.subtype != " + sub
	}
	return cond
}

// pyElemWireGuard is pyWireGuard for a wrapper-sequence ELEMENT: §5.1 makes each
// element a normal field, so §7.3 applies — an element whose wire type/subtype
// contradicts the declared element type is skipped like an unknown id (issue
// #189), not fed to the schema-typed reader. Named by the element's header
// variable `ef` (`_ef<depth>`) rather than the field's `fld`, and mapped through
// a synthetic field so it stays in lockstep with the field-level guard.
func pyElemWireGuard(ef string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem) string {
	x := &ir.Field{Kind: elem, Ref: ref}
	if elem == ir.KindArray {
		x.Elem = items.Elem
		x.ElemRef = items.ElemRef
	}
	cond := ef + ".type != " + pyExpectedWire(x)
	sub := pyFixlenSubtype(elem)
	if elem == ir.KindArray && isNativeArrayElem(items.Elem) {
		sub = pyFixlenSubtype(items.Elem)
	}
	if sub != "" {
		cond += " or " + ef + ".subtype != " + sub
	}
	return cond
}

// schemaHasField reports whether any message or named-type field satisfies pred.
func schemaHasField(s *ir.Schema, pred func(*ir.Field) bool) bool {
	any := func(fields []*ir.Field) bool {
		for _, fld := range fields {
			if pred(fld) {
				return true
			}
		}
		return false
	}
	for _, key := range s.NamedOrder {
		if any(s.Named[key].Fields) {
			return true
		}
	}
	for _, m := range s.Messages {
		if any(m.Fields) {
			return true
		}
	}
	return false
}

// emitFixedArrayHelpers emits the module-private helpers a fixed-count (`count:
// N`) native array's canonical encoding needs (MESSAGE_SPEC §3): _trim_tail /
// _trim_tail_float drop the trailing element-default run on encode, _pad_to
// rebuilds it on decode. Emitted only when the schema actually has such a
// field, so other schemas keep a helper-free module.
func (g *gen) emitFixedArrayHelpers(f *pyfile, s *ir.Schema) {
	if !schemaHasCountedNativeArray(s) {
		return
	}
	f.line("def _trim_tail(a: list, zero) -> list:")
	f.line(`    """Return a[:M'], M' being one past the last non-default element (0 if all`)
	f.line("    default). A fixed-count array's canonical wire carries exactly those M'")
	f.line("    elements; the decoder rebuilds the trailing default run from the schema")
	f.line(`    count (MESSAGE_SPEC S3)."""`)
	f.line("    n = len(a)")
	f.line("    while n > 0 and a[n - 1] == zero:")
	f.line("        n -= 1")
	f.line("    return a[:n]")
	f.blank()
	if schemaHasCountedFloatArray(s) {
		f.line("def _trim_tail_float(a: list) -> list:")
		f.line(`    """_trim_tail for floats, comparing by BIT PATTERN rather than ==: a`)
		f.line("    trailing -0.0 (which == 0.0) and a trailing NaN are distinct values and")
		f.line(`    must survive the round-trip instead of being trimmed to +0.0."""`)
		f.line("    n = len(a)")
		f.line("    while n > 0 and a[n - 1] == 0.0 and math.copysign(1.0, a[n - 1]) > 0.0:")
		f.line("        n -= 1")
		f.line("    return a[:n]")
		f.blank()
	}
	f.line("def _pad_to(a: list, n: int, zero) -> list:")
	f.line(`    """Return a grown to exactly n elements with the element default. A`)
	f.line("    fixed-count array decodes to exactly its schema count regardless of the")
	f.line("    wire count, so the trailing default run the encoder elided is")
	f.line(`    materialized here (MESSAGE_SPEC S3)."""`)
	f.line("    if len(a) >= n:")
	f.line("        return a")
	f.line("    return a + [zero] * (n - len(a))")
	f.blank()
}

func (g *gen) emitEnum(f *pyfile, nt *ir.NamedType) {
	f.line("class %s(IntEnum):", g.typeName(nt.Key))
	for _, c := range nt.Consts {
		// Sphinx attribute comment(s) carrying the constant's description, above
		// the member so pydoc/Sphinx attaches it to the enum value.
		if c.Description != "" {
			for _, dl := range strings.Split(c.Description, "\n") {
				f.line("    #: %s", dl)
			}
		}
		f.line("    %s = %d", strings.ToUpper(c.Name), c.Value)
	}
	f.blank()
}

func (g *gen) emitBitfieldConsts(f *pyfile, nt *ir.NamedType) {
	f.line("class %s(IntEnum):", g.typeName(nt.Key))
	for _, fl := range nt.Flags {
		// Sphinx attribute comment(s): the flag description, with the schema
		// default appended as "(default: true/false)" when the flag has one.
		for _, dl := range bitfieldFlagDocLines(fl) {
			f.line("    #: %s", dl)
		}
		f.line("    %s = 1 << %d", strings.ToUpper(fl.Name), fl.Pos)
	}
	f.blank()
}

// bitfieldFlagDocLines builds the Sphinx attribute-comment text (without the
// leading "#: ") for a bitfield flag: its Description, plus a trailing
// "(default: true)" / "(default: false)" note appended to the last line when
// the flag declares a default. Returns nil when there is nothing to document.
func bitfieldFlagDocLines(fl *ir.BitfieldFlag) []string {
	var lines []string
	if fl.Description != "" {
		lines = strings.Split(fl.Description, "\n")
	}
	if fl.HasDefault {
		note := "(default: false)"
		if fl.Default {
			note = "(default: true)"
		}
		if len(lines) == 0 {
			lines = []string{note}
		} else {
			lines[len(lines)-1] = lines[len(lines)-1] + " " + note
		}
	}
	return lines
}

// emitClassDoc writes a triple-quoted class docstring as the first statement of
// the class body when the summary is non-empty. A single-line summary becomes
// """<summary>"""; a multi-line summary opens with """ on the first line and
// closes with """ on its own line. UTF-8 passes through byte-for-byte.
func emitClassDoc(f *pyfile, summary string) {
	if summary == "" {
		return
	}
	lines := strings.Split(summary, "\n")
	if len(lines) == 1 {
		f.line(`    """%s"""`, lines[0])
		return
	}
	f.line(`    """%s`, lines[0])
	for _, ln := range lines[1:] {
		f.line("    %s", ln)
	}
	f.line(`    """`)
}

// pyFieldDocLines builds the Sphinx attribute-comment text (without the leading
// "#: ") from a field's Description and Unit. A multi-line description yields one
// line per source line; a non-empty Unit is appended as " (unit: <Unit>)" to the
// last line. A deprecated field gets a trailing ".. deprecated::" directive so
// Sphinx renders the deprecation and pydoc surfaces the note. Returns nil when
// there is nothing to document.
func pyFieldDocLines(fld *ir.Field) []string {
	var lines []string
	if fld.Description != "" {
		lines = strings.Split(fld.Description, "\n")
	}
	if fld.Unit != "" {
		if len(lines) == 0 {
			lines = []string{fmt.Sprintf("(unit: %s)", fld.Unit)}
		} else {
			lines[len(lines)-1] = fmt.Sprintf("%s (unit: %s)", lines[len(lines)-1], fld.Unit)
		}
	}
	if fld.Deprecated {
		lines = append(lines,
			".. deprecated::",
			"   Deprecated and retained only for backward compatibility; do not use in new code.")
	}
	return lines
}

func (g *gen) emitDataclass(f *pyfile, name, summary string, fields []*ir.Field) {
	f.line("@dataclass")
	f.line("class %s:", name)
	// Class docstring (pydoc/Sphinx) as the first statement in the body, when the
	// summary is non-empty. It also satisfies the body for a field-less class.
	emitClassDoc(f, summary)
	if len(fields) == 0 && summary == "" {
		f.line("    pass")
	}
	for _, fld := range fields {
		// Sphinx attribute comment(s) immediately before the declaration.
		for _, dl := range pyFieldDocLines(fld) {
			f.line("    #: %s", dl)
		}
		f.line("    %s: %s = %s", pyIdent(fld.Name), g.pyAnnot(fld), g.pyDefault(fld))
	}
	f.blank()

	// _marshal
	f.line("    def _marshal(self, e: Encoder) -> None:")
	if len(fields) == 0 {
		f.line("        pass")
	}
	for _, fld := range fields {
		g.emitMarshal(f, fld)
	}
	f.blank()

	// _unmarshal (pull-parser)
	f.line("    def _unmarshal(self, d: Decoder) -> None:")
	f.line("        while True:")
	f.line("            fld = d.next()")
	f.line("            if fld is None or fld.type == WireType.SEQUENCE_END:")
	f.line("                return")
	for i, fld := range fields {
		kw := "elif"
		if i == 0 {
			kw = "if"
		}
		f.line("            %s fld.id == %d:", kw, fld.ID)
		// Frame each field by its header wire type before reading (MESSAGE_SPEC
		// §7.3). The schema-typed readers assume they are only called for their
		// matching wire type — corelib-py raises SofaStateError from _take_scalar /
		// _take_fixlen when they are not, which is the right answer to a *caller*
		// error but the wrong outcome here: a field whose header contradicts the
		// schema must be skipped like an unknown id, not fail the whole decode.
		// The wire type is already in hand (fld.type is read above), so the guard
		// costs one comparison and keeps the decoder synced.
		f.line("                if %s:", pyWireGuard(fld))
		f.line("                    d.skip()")
		f.line("                    continue")
		g.emitUnmarshal(f, fld)
	}
	if len(fields) > 0 {
		f.line("            else:")
		f.line("                d.skip()")
	} else {
		f.line("            d.skip()")
	}
	f.blank()

	// JSON helpers + encode/decode
	g.emitJSON(f, name, fields)
}

func (g *gen) emitMarshal(f *pyfile, fld *ir.Field) {
	acc := "self." + pyIdent(fld.Name)
	var write string
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		write = fmt.Sprintf("e.write_unsigned(%d, int(%s))", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		write = fmt.Sprintf("e.write_signed(%d, int(%s))", fld.ID, acc)
	case ir.KindBool:
		write = fmt.Sprintf("e.write_bool(%d, %s)", fld.ID, acc)
	case ir.KindFP32:
		write = fmt.Sprintf("e.write_float32(%d, %s)", fld.ID, acc)
	case ir.KindFP64:
		write = fmt.Sprintf("e.write_float64(%d, %s)", fld.ID, acc)
	case ir.KindString:
		write = fmt.Sprintf("e.write_string(%d, %s)", fld.ID, acc)
	case ir.KindBlob:
		// blob is a leaf: omit when equal to its default (empty if none).
		f.line("        if bytes(%s) != %s:", acc, g.pyDefault(fld))
		f.line("            e.write_bytes(%d, bytes(%s))", fld.ID, acc)
		return
	case ir.KindStruct, ir.KindUnion:
		// A sequence is always framed; its child fields are omitted per-field by
		// the nested marshal (MESSAGE_SPEC S2). An all-default nested object thus
		// becomes an empty wrapper sequence, not a dropped field.
		f.line("        e.write_sequence_begin(%d)", fld.ID)
		f.line("        %s._marshal(e)", acc)
		f.line("        e.write_sequence_end()")
		return
	case ir.KindArray:
		g.emitMarshalArray(f, fld, acc)
		return
	}
	// Scalar/string/enum/bitfield leaf: always omit when equal to the default;
	// sparse encoding is canonical (MESSAGE_SPEC S2) and the decoder reconstructs
	// the omitted field from its default.
	f.line("        if %s != %s:", acc, g.pyDefault(fld))
	f.line("            %s", write)
}

func (g *gen) emitMarshalArray(f *pyfile, fld *ir.Field, acc string) {
	// A native scalar array is a leaf field: omit it when equal to its default
	// (materialized in the dataclass), else when empty. A composite/dynamic-element
	// array is a wrapper sequence and is always framed (never whole-omitted).
	if isNativeArrayElem(fld.Elem) {
		if lit, ok := g.pyNativeArrayDefault(fld); ok {
			f.line("        if %s != %s:", acc, lit)
		} else {
			f.line("        if len(%s) != 0:", acc)
		}
		g.marshalArray(f, "            ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, fld.HasCount, 0)
		return
	}
	g.marshalArray(f, "        ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, fld.HasCount, 0)
}

// trimExpr wraps a native array expression in the trailing-default-run trim a
// fixed-count array's canonical encoding requires (MESSAGE_SPEC §3). Only a
// declared `count: N` array is fixed-length; a dynamic (count-less) array has no
// N to refill from, so a trailing default element is significant and stays.
func trimExpr(val string, elem ir.Kind, fixed bool) string {
	if !fixed {
		return val
	}
	switch elem {
	case ir.KindFP32, ir.KindFP64:
		return fmt.Sprintf("_trim_tail_float(%s)", val)
	default:
		// enum/bitfield/bool have already been lowered to their int image, whose
		// zero is the element default; ints compare by value == bit pattern.
		return fmt.Sprintf("_trim_tail(%s, 0)", val)
	}
}

// marshalArray writes the array `val` as field `idExpr`. Numeric/enum/boolean/
// bitfield elements use the native array wire type (enum->signed, bool/bitfield->
// unsigned); string/blob/struct/union/array elements lower to a wrapper sequence
// whose child ids are the 0-based index (per MESSAGE_SPEC). Recurses for nested
// arrays.
func (g *gen) marshalArray(f *pyfile, ind, idExpr, val string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, fixed bool, depth int) {
	iv := fmt.Sprintf("_i%d", depth)
	ev := fmt.Sprintf("_e%d", depth)
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64:
		f.line("%se.write_unsigned_array(%s, %s)", ind, idExpr, trimExpr(val, elem, fixed))
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
		f.line("%se.write_signed_array(%s, %s)", ind, idExpr, trimExpr(val, elem, fixed))
	case ir.KindEnum:
		f.line("%se.write_signed_array(%s, %s)", ind, idExpr, trimExpr(fmt.Sprintf("[int(_v) for _v in %s]", val), elem, fixed))
	case ir.KindBool:
		// Trimming the 0/1 image is equivalent to trimming the bools (False <-> 0).
		f.line("%se.write_unsigned_array(%s, %s)", ind, idExpr, trimExpr(fmt.Sprintf("[1 if _v else 0 for _v in %s]", val), elem, fixed))
	case ir.KindBitfield:
		f.line("%se.write_unsigned_array(%s, %s)", ind, idExpr, trimExpr(fmt.Sprintf("[int(_v) for _v in %s]", val), elem, fixed))
	case ir.KindFP32:
		f.line("%se.write_float32_array(%s, %s)", ind, idExpr, trimExpr(val, elem, fixed))
	case ir.KindFP64:
		f.line("%se.write_float64_array(%s, %s)", ind, idExpr, trimExpr(val, elem, fixed))
	case ir.KindString:
		// A string element is a leaf: omit it when equal to the element default
		// (empty), leaving an id gap the decoder restores (MESSAGE_SPEC S2).
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		f.line(`%s    if %s != "":`, ind, ev)
		f.line("%s        e.write_string(%s, %s)", ind, iv, ev)
		f.line("%se.write_sequence_end()", ind)
	case ir.KindBlob:
		// A blob element is a leaf: omit it when equal to the element default
		// (empty), leaving an id gap the decoder restores (MESSAGE_SPEC S2).
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		f.line("%s    if len(%s) != 0:", ind, ev)
		f.line("%s        e.write_bytes(%s, bytes(%s))", ind, iv, ev)
		f.line("%se.write_sequence_end()", ind)
	case ir.KindStruct, ir.KindUnion:
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		f.line("%s    e.write_sequence_begin(%s)", ind, iv)
		f.line("%s    %s._marshal(e)", ind, ev)
		f.line("%s    e.write_sequence_end()", ind)
		f.line("%se.write_sequence_end()", ind)
	case ir.KindArray:
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		// A nested array-of-array row is an ir.ArrayElem, not a field: the
		// fixed-length rule is scoped to top-level array FIELDS, so a row never
		// trims (MESSAGE_SPEC §3).
		g.marshalArray(f, ind+"    ", iv, ev, items.Elem, items.ElemRef, items.ElemItems, false, depth+1)
		f.line("%se.write_sequence_end()", ind)
	}
}

func (g *gen) emitUnmarshal(f *pyfile, fld *ir.Field) {
	acc := "self." + pyIdent(fld.Name)
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("                %s = d.unsigned()", acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("                %s = d.signed()", acc)
	case ir.KindBool:
		f.line("                %s = d.bool()", acc)
	case ir.KindFP32:
		f.line("                %s = d.float32()", acc)
	case ir.KindFP64:
		f.line("                %s = d.float64()", acc)
	case ir.KindString:
		// A bounded string whose UTF-8 BYTE length exceeds its schema maxlen is
		// malformed input — reject, never truncate (MESSAGE_SPEC §7.1). Bound
		// against the exact wire byte length the decoder already parsed
		// (d.fixlen_len(), a non-consuming peek), checked before the string is
		// materialized — never re-encode the decoded str to measure it.
		if fld.HasMaxlen {
			f.line("                if d.fixlen_len() > %d:", fld.Maxlen)
			f.line(`                    raise SofaDecodeError("%s: string byte length above schema maxlen %d")`, fld.Name, fld.Maxlen)
		}
		f.line("                %s = d.string()", acc)
	case ir.KindBlob:
		f.line("                %s = d.bytes()", acc)
		// A bounded blob whose decoded byte length exceeds its schema maxlen is
		// malformed input — reject, never truncate (MESSAGE_SPEC §7.1).
		if fld.HasMaxlen {
			f.line("                if len(%s) > %d:", acc, fld.Maxlen)
			f.line(`                    raise SofaDecodeError("%s: blob byte length above schema maxlen %d")`, fld.Name, fld.Maxlen)
		}
	case ir.KindStruct, ir.KindUnion:
		f.line("                %s._unmarshal(d)", acc)
	case ir.KindArray:
		g.emitUnmarshalArray(f, fld, acc)
	}
}

// capOf maps a schema fixed-count bound to a wrapper array's cap: N when the
// array declares a count, -1 (dynamic/unbounded) otherwise.
func capOf(hasCount bool, count int64) int64 {
	if hasCount {
		return count
	}
	return -1
}

func (g *gen) emitUnmarshalArray(f *pyfile, fld *ir.Field, acc string) {
	g.unmarshalArray(f, "                ", acc, fld.Elem, fld.ElemRef, fld.ElemItems, capOf(fld.HasCount, fld.Count), fld.ElemMaxHas, fld.ElemMax, 0)
	// A wire element count above the schema `count` capacity is INVALID per
	// MESSAGE_SPEC §3+§7 — reject the whole message, never keep-all
	// (generator#100). Count-less (dynamic) arrays have no bound.
	if fld.HasCount && isNativeArrayElem(fld.Elem) {
		f.line("                if len(%s) > %d:", acc, fld.Count)
		f.line(`                    raise SofaDecodeError("%s: array count above schema capacity %d")`, fld.Name, fld.Count)
		// A `count: N` array is fixed-length: the wire may carry M <= N elements,
		// and positions [M, N) are the element default. A growable container must
		// materialize them so the decoded value has exactly N elements on every
		// storage model (MESSAGE_SPEC §3).
		f.line("                %s = _pad_to(%s, %d, %s)", acc, acc, fld.Count, pyElemZero(fld.Elem))
	}
}

// pyElemZero is the element default of a native array element, as the Python
// value the decode path materializes (enum/bitfield elements decode to their
// int image, matching unmarshalArray).
func pyElemZero(elem ir.Kind) string {
	switch elem {
	case ir.KindBool:
		return "False"
	case ir.KindFP32, ir.KindFP64:
		return "0.0"
	default:
		return "0"
	}
}

// unmarshalArray reads an array into `target`, mirroring marshalArray: native
// array readers for numeric/enum/boolean/bitfield elements, a wrapper-sequence
// loop for string/blob/struct/union/array elements. Recurses for nested arrays.
func (g *gen) unmarshalArray(f *pyfile, ind, target string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, cap int64, elemMaxHas bool, elemMax int64, depth int) {
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("%s%s = d.read_unsigned_array()", ind, target)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("%s%s = d.read_signed_array()", ind, target)
	case ir.KindBool:
		f.line("%s%s = [bool(_v) for _v in d.read_unsigned_array()]", ind, target)
	case ir.KindFP32:
		f.line("%s%s = d.read_float32_array()", ind, target)
	case ir.KindFP64:
		f.line("%s%s = d.read_float64_array()", ind, target)
	default: // string/blob/struct/union/array -> wrapper sequence
		ef := fmt.Sprintf("_ef%d", depth)
		ev := fmt.Sprintf("_e%d", depth)
		f.line("%s%s = []", ind, target)
		f.line("%swhile True:", ind)
		f.line("%s    %s = d.next()", ind, ef)
		f.line("%s    if %s is None or %s.type == WireType.SEQUENCE_END:", ind, ef, ef)
		f.line("%s        break", ind)
		// §7.3 (issue #189): an element whose wire type/subtype contradicts the
		// declared element type is skipped like an unknown id — before the
		// over-index check, so a mis-typed element is skipped, not rejected for its
		// id — never handed to the schema-typed reader (which would raise).
		f.line("%s    if %s:", ind, pyElemWireGuard(ef, elem, ref, items))
		f.line("%s        d.skip()", ind)
		f.line("%s        continue", ind)
		// Fixed-count wrapper array: an element id >= N is INVALID (MESSAGE_SPEC
		// §5.1/§7 — issue #142), rejected before the list grows, which also bounds
		// an over-index heap-amplification fill. A dynamic array keeps every index.
		if cap >= 0 {
			f.line("%s    if %s.id >= %d:", ind, ef, cap)
			f.line(`%s        raise SofaDecodeError("%s: array index above schema capacity %d")`, ind, target, cap)
		}
		switch elem {
		case ir.KindString:
			// A string element is keyed by index id: a default (empty) element is
			// omitted on the wire, so place the value at its id and fill any gap
			// with the element default "" (MESSAGE_SPEC S2).
			f.line("%s    while len(%s) <= %s.id:", ind, target, ef)
			f.line(`%s        %s.append("")`, ind, target)
			// A bounded string element whose UTF-8 BYTE length exceeds the element
			// maxlen is malformed — reject, never truncate (MESSAGE_SPEC §7.1). Bound
			// against the wire byte length (non-consuming peek) before the element is
			// materialized — never re-encode the decoded str to measure it.
			if elemMaxHas {
				f.line(`%s    if d.fixlen_len() > %d:`, ind, elemMax)
				f.line(`%s        raise SofaDecodeError("%s: string element byte length above schema maxlen %d")`, ind, target, elemMax)
			}
			f.line("%s    %s[%s.id] = d.string()", ind, target, ef)
		case ir.KindBlob:
			// A blob element is keyed by index id: a default (empty) element is
			// omitted on the wire, so place the value at its id and fill any gap
			// with the element default b"" (MESSAGE_SPEC S2).
			f.line("%s    while len(%s) <= %s.id:", ind, target, ef)
			f.line(`%s        %s.append(b"")`, ind, target)
			f.line("%s    %s[%s.id] = d.bytes()", ind, target, ef)
			// A bounded blob element whose byte length exceeds the element maxlen
			// is malformed — reject, never truncate (MESSAGE_SPEC §7.1).
			if elemMaxHas {
				f.line("%s    if len(%s[%s.id]) > %d:", ind, target, ef, elemMax)
				f.line(`%s        raise SofaDecodeError("%s: blob element byte length above schema maxlen %d")`, ind, target, elemMax)
			}
		case ir.KindStruct, ir.KindUnion:
			f.line("%s    %s = %s()", ind, ev, g.typeName(ref.Key))
			f.line("%s    %s._unmarshal(d)", ind, ev)
			f.line("%s    %s.append(%s)", ind, target, ev)
		case ir.KindArray:
			g.unmarshalArray(f, ind+"    ", ev, items.Elem, items.ElemRef, items.ElemItems, capOf(items.HasCount, items.Count), items.ElemMaxHas, items.ElemMax, depth+1)
			f.line("%s    %s.append(%s)", ind, target, ev)
		}
	}
}
