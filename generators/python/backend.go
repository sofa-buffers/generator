// Package python is the Python throughput backend (PLAN §6.4): it emits
// dataclasses with _marshal (Encoder) and a pull-parser _unmarshal
// (Decoder.next/skip + typed readers) against corelib-py, plus JSON helpers for
// the conformance harness.
package python

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

func init() { generator.Register(&Backend{}) }

// Backend implements generator.Backend for Python.
type Backend struct{}

func (*Backend) Lang() string { return "python" }

// Generate emits a single module (all enums/bitfields/dataclasses + messages).
// In project mode it adds a harness and pyproject.toml.
func (*Backend) Generate(s *ir.Schema, cfg map[string]any) ([]generator.File, error) {
	g := &gen{schema: s, banner: cfgString(cfg, "tool_banner", "sofabgen"), license: generator.LicenseID(cfg)}
	module := g.module(s)
	files := []generator.File{{Path: "message.py", Content: module}}
	if cfgString(cfg, "emit", "sources") == "project" {
		files = append(files, g.projectFiles(s, cfg)...)
	}
	return files, nil
}

type gen struct {
	schema  *ir.Schema
	banner  string
	license string // SPDX id, "" to omit the header line
}

type pyfile struct{ b strings.Builder }

func (f *pyfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}
func (f *pyfile) blank()        { f.b.WriteByte('\n') }
func (f *pyfile) bytes() []byte { return []byte(f.b.String()) }

func (g *gen) module(s *ir.Schema) []byte {
	f := &pyfile{}
	f.line("# Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("# SPDX-License-Identifier: %s", g.license)
	}
	f.line("from __future__ import annotations")
	f.line("import io")
	f.line("from dataclasses import dataclass, field")
	f.line("from enum import IntEnum")
	f.line("from sofab import Encoder, Decoder, WireType")
	f.blank()

	// enums + bitfield constants first
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		switch nt.Category {
		case ir.CatEnum:
			g.emitEnum(f, nt)
		case ir.CatBitfield:
			g.emitBitfieldConsts(f, nt)
		}
	}
	// struct/union dataclasses, then messages
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitDataclass(f, g.typeName(key), nt.Summary, nt.Fields)
		}
	}
	for _, m := range s.Messages {
		g.emitDataclass(f, exported(m.Name), m.Summary, m.Fields)
	}
	return f.bytes()
}

func (g *gen) emitEnum(f *pyfile, nt *ir.NamedType) {
	f.line("class %s(IntEnum):", g.typeName(nt.Key))
	for _, c := range nt.Consts {
		f.line("    %s = %d", strings.ToUpper(c.Name), c.Value)
	}
	f.blank()
}

func (g *gen) emitBitfieldConsts(f *pyfile, nt *ir.NamedType) {
	f.line("class %s(IntEnum):", g.typeName(nt.Key))
	for _, fl := range nt.Flags {
		f.line("    %s = 1 << %d", strings.ToUpper(fl.Name), fl.Pos)
	}
	f.blank()
}

// emitClassDoc writes a triple-quoted class docstring as the first statement of
// the class body when the summary is non-empty. A single-line summary becomes
// """<summary>"""; a multi-line summary opens with """ on the first line and
// closes with """ on its own line. UTF-8 passes through byte-for-byte.
func emitClassDoc(f *pyfile, summary string) {
	if summary == "" {
		return
	}
	lines := strings.Split(summary, "\n")
	if len(lines) == 1 {
		f.line(`    """%s"""`, lines[0])
		return
	}
	f.line(`    """%s`, lines[0])
	for _, ln := range lines[1:] {
		f.line("    %s", ln)
	}
	f.line(`    """`)
}

// pyFieldDocLines builds the Sphinx attribute-comment text (without the leading
// "#: ") from a field's Description and Unit. A multi-line description yields one
// line per source line; a non-empty Unit is appended as " (unit: <Unit>)" to the
// last line. Returns nil when both are empty.
func pyFieldDocLines(fld *ir.Field) []string {
	if fld.Description == "" && fld.Unit == "" {
		return nil
	}
	if fld.Description == "" {
		return []string{fmt.Sprintf("(unit: %s)", fld.Unit)}
	}
	lines := strings.Split(fld.Description, "\n")
	if fld.Unit != "" {
		lines[len(lines)-1] = fmt.Sprintf("%s (unit: %s)", lines[len(lines)-1], fld.Unit)
	}
	return lines
}

func (g *gen) emitDataclass(f *pyfile, name, summary string, fields []*ir.Field) {
	f.line("@dataclass")
	f.line("class %s:", name)
	// Class docstring (pydoc/Sphinx) as the first statement in the body, when the
	// summary is non-empty. It also satisfies the body for a field-less class.
	emitClassDoc(f, summary)
	if len(fields) == 0 && summary == "" {
		f.line("    pass")
	}
	for _, fld := range fields {
		// Sphinx attribute comment(s) immediately before the declaration.
		for _, dl := range pyFieldDocLines(fld) {
			f.line("    #: %s", dl)
		}
		f.line("    %s: %s = %s", pyIdent(fld.Name), g.pyAnnot(fld), g.pyDefault(fld))
	}
	f.blank()

	// _marshal
	f.line("    def _marshal(self, e: Encoder) -> None:")
	if len(fields) == 0 {
		f.line("        pass")
	}
	for _, fld := range fields {
		g.emitMarshal(f, fld)
	}
	f.blank()

	// _unmarshal (pull-parser)
	f.line("    def _unmarshal(self, d: Decoder) -> None:")
	f.line("        while True:")
	f.line("            fld = d.next()")
	f.line("            if fld is None or fld.type == WireType.SEQUENCE_END:")
	f.line("                return")
	for i, fld := range fields {
		kw := "elif"
		if i == 0 {
			kw = "if"
		}
		f.line("            %s fld.id == %d:", kw, fld.ID)
		g.emitUnmarshal(f, fld)
	}
	if len(fields) > 0 {
		f.line("            else:")
		f.line("                d.skip()")
	} else {
		f.line("            d.skip()")
	}
	f.blank()

	// JSON helpers + encode/decode
	g.emitJSON(f, name, fields)
}

func (g *gen) emitMarshal(f *pyfile, fld *ir.Field) {
	acc := "self." + pyIdent(fld.Name)
	var write string
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		write = fmt.Sprintf("e.write_unsigned(%d, int(%s))", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		write = fmt.Sprintf("e.write_signed(%d, int(%s))", fld.ID, acc)
	case ir.KindBool:
		write = fmt.Sprintf("e.write_bool(%d, %s)", fld.ID, acc)
	case ir.KindFP32:
		write = fmt.Sprintf("e.write_float32(%d, %s)", fld.ID, acc)
	case ir.KindFP64:
		write = fmt.Sprintf("e.write_float64(%d, %s)", fld.ID, acc)
	case ir.KindString:
		write = fmt.Sprintf("e.write_string(%d, %s)", fld.ID, acc)
	case ir.KindBlob:
		// blob is a leaf: omit when equal to its default (empty if none).
		f.line("        if bytes(%s) != %s:", acc, g.pyDefault(fld))
		f.line("            e.write_bytes(%d, bytes(%s))", fld.ID, acc)
		return
	case ir.KindStruct, ir.KindUnion:
		// A sequence is always framed; its child fields are omitted per-field by
		// the nested marshal (MESSAGE_SPEC S2). An all-default nested object thus
		// becomes an empty wrapper sequence, not a dropped field.
		f.line("        e.write_sequence_begin(%d)", fld.ID)
		f.line("        %s._marshal(e)", acc)
		f.line("        e.write_sequence_end()")
		return
	case ir.KindArray:
		g.emitMarshalArray(f, fld, acc)
		return
	}
	// Scalar/string/enum/bitfield leaf: always omit when equal to the default;
	// sparse encoding is canonical (MESSAGE_SPEC S2) and the decoder reconstructs
	// the omitted field from its default.
	f.line("        if %s != %s:", acc, g.pyDefault(fld))
	f.line("            %s", write)
}

func (g *gen) emitMarshalArray(f *pyfile, fld *ir.Field, acc string) {
	// A native scalar array is a leaf field: omit it when equal to its default
	// (materialized in the dataclass), else when empty. A composite/dynamic-element
	// array is a wrapper sequence and is always framed (never whole-omitted).
	if isNativeArrayElem(fld.Elem) {
		if lit, ok := g.pyNativeArrayLiteral(fld); ok {
			f.line("        if %s != %s:", acc, lit)
		} else {
			f.line("        if len(%s) != 0:", acc)
		}
		g.marshalArray(f, "            ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0)
		return
	}
	g.marshalArray(f, "        ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0)
}

// marshalArray writes the array `val` as field `idExpr`. Numeric/enum/boolean/
// bitfield elements use the native array wire type (enum->signed, bool/bitfield->
// unsigned); string/blob/struct/union/array elements lower to a wrapper sequence
// whose child ids are the 0-based index (per MESSAGE_SPEC). Recurses for nested
// arrays.
func (g *gen) marshalArray(f *pyfile, ind, idExpr, val string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int) {
	iv := fmt.Sprintf("_i%d", depth)
	ev := fmt.Sprintf("_e%d", depth)
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64:
		f.line("%se.write_unsigned_array(%s, %s)", ind, idExpr, val)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
		f.line("%se.write_signed_array(%s, %s)", ind, idExpr, val)
	case ir.KindEnum:
		f.line("%se.write_signed_array(%s, [int(_v) for _v in %s])", ind, idExpr, val)
	case ir.KindBool:
		f.line("%se.write_unsigned_array(%s, [1 if _v else 0 for _v in %s])", ind, idExpr, val)
	case ir.KindBitfield:
		f.line("%se.write_unsigned_array(%s, [int(_v) for _v in %s])", ind, idExpr, val)
	case ir.KindFP32:
		f.line("%se.write_float32_array(%s, %s)", ind, idExpr, val)
	case ir.KindFP64:
		f.line("%se.write_float64_array(%s, %s)", ind, idExpr, val)
	case ir.KindString:
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		f.line("%s    e.write_string(%s, %s)", ind, iv, ev)
		f.line("%se.write_sequence_end()", ind)
	case ir.KindBlob:
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		f.line("%s    e.write_bytes(%s, bytes(%s))", ind, iv, ev)
		f.line("%se.write_sequence_end()", ind)
	case ir.KindStruct, ir.KindUnion:
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		f.line("%s    e.write_sequence_begin(%s)", ind, iv)
		f.line("%s    %s._marshal(e)", ind, ev)
		f.line("%s    e.write_sequence_end()", ind)
		f.line("%se.write_sequence_end()", ind)
	case ir.KindArray:
		f.line("%se.write_sequence_begin(%s)", ind, idExpr)
		f.line("%sfor %s, %s in enumerate(%s):", ind, iv, ev, val)
		g.marshalArray(f, ind+"    ", iv, ev, items.Elem, items.ElemRef, items.ElemItems, depth+1)
		f.line("%se.write_sequence_end()", ind)
	}
}

func (g *gen) emitUnmarshal(f *pyfile, fld *ir.Field) {
	acc := "self." + pyIdent(fld.Name)
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("                %s = d.unsigned()", acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("                %s = d.signed()", acc)
	case ir.KindBool:
		f.line("                %s = d.bool()", acc)
	case ir.KindFP32:
		f.line("                %s = d.float32()", acc)
	case ir.KindFP64:
		f.line("                %s = d.float64()", acc)
	case ir.KindString:
		f.line("                %s = d.string()", acc)
	case ir.KindBlob:
		f.line("                %s = d.bytes()", acc)
	case ir.KindStruct, ir.KindUnion:
		f.line("                %s._unmarshal(d)", acc)
	case ir.KindArray:
		g.emitUnmarshalArray(f, fld, acc)
	}
}

func (g *gen) emitUnmarshalArray(f *pyfile, fld *ir.Field, acc string) {
	g.unmarshalArray(f, "                ", acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0)
}

// unmarshalArray reads an array into `target`, mirroring marshalArray: native
// array readers for numeric/enum/boolean/bitfield elements, a wrapper-sequence
// loop for string/blob/struct/union/array elements. Recurses for nested arrays.
func (g *gen) unmarshalArray(f *pyfile, ind, target string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int) {
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("%s%s = d.read_unsigned_array()", ind, target)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("%s%s = d.read_signed_array()", ind, target)
	case ir.KindBool:
		f.line("%s%s = [bool(_v) for _v in d.read_unsigned_array()]", ind, target)
	case ir.KindFP32:
		f.line("%s%s = d.read_float32_array()", ind, target)
	case ir.KindFP64:
		f.line("%s%s = d.read_float64_array()", ind, target)
	default: // string/blob/struct/union/array -> wrapper sequence
		ef := fmt.Sprintf("_ef%d", depth)
		ev := fmt.Sprintf("_e%d", depth)
		f.line("%s%s = []", ind, target)
		f.line("%swhile True:", ind)
		f.line("%s    %s = d.next()", ind, ef)
		f.line("%s    if %s is None or %s.type == WireType.SEQUENCE_END:", ind, ef, ef)
		f.line("%s        break", ind)
		switch elem {
		case ir.KindString:
			f.line("%s    %s.append(d.string())", ind, target)
		case ir.KindBlob:
			f.line("%s    %s.append(d.bytes())", ind, target)
		case ir.KindStruct, ir.KindUnion:
			f.line("%s    %s = %s()", ind, ev, g.typeName(ref.Key))
			f.line("%s    %s._unmarshal(d)", ind, ev)
			f.line("%s    %s.append(%s)", ind, target, ev)
		case ir.KindArray:
			g.unmarshalArray(f, ind+"    ", ev, items.Elem, items.ElemRef, items.ElemItems, depth+1)
			f.line("%s    %s.append(%s)", ind, target, ev)
		}
	}
}
