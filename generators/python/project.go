// projectFiles scaffolds a runnable Python project: an encode/decode JSON
// harness over the generated module + a pyproject.toml (PLAN §9.1).
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	return []generator.File{
		{Path: "harness.py", Content: g.harness(s)},
		{Path: "pyproject.toml", Content: []byte(pyproject)},
		{Path: "README.md", Content: []byte(readme)},
	}
}

const pyproject = `# @generated by sofabgen
[project]
name = "sofabuffers-generated"
version = "0.0.0"
requires-python = ">=3.9"
dependencies = ["sofa-buffers-corelib"]
`

const readme = "# Generated SofaBuffers Python module\n\n" +
	"`@generated by sofabgen`. Encode/decode harness over `corelib-py`.\n\n" +
	"```sh\n# with corelib-py on PYTHONPATH:\n" +
	"echo '{...}' | python harness.py encode <Message>   # JSON -> bytes\n" +
	"python harness.py decode <Message> < bytes.bin       # bytes -> JSON\n```\n"

// benchSink returns an expression that folds one cheap integer scalar out of
// `expr`, so the decode in the measured loop cannot be elided without also paying
// for the fold. It must stay cheap: it runs inside the rep loop, so anything
// heavier (to_jsonable, repr) would be measured as if it were decode cost.
//
// Falls back to id() when a message has no integer scalar at all — still forces the
// decode, just less precisely.
func benchSink(m *ir.Message, expr string) string {
	for _, f := range m.Fields {
		switch f.Kind {
		case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64,
			ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
			return expr + "." + f.Name
		}
	}
	return "id(" + expr + ")"
}

// emitBench emits the `bench <workload> <reps>` entry point (tests/bench,
// ARCHITECTURE §15).
//
// CPython has no native symbol to --toggle-collect on (the work happens inside the
// interpreter loop), so this is the `subtract` method: run the same workload at two
// rep counts and let tests/bench subtract the totals —
//
//	Ir/op = ( Ir(R2) - Ir(R1) ) / ( R2 - R1 )
//
// which cancels interpreter startup, imports, JSON parsing and setup exactly. That
// only holds if the two runs differ in NOTHING but the rep count, hence
// gc.disable(): a collection that fires in one run and not the other lands entirely
// in the delta. Same shape as corelib-py/bench/run_callgrind.sh.
func (g *gen) emitBench(f *pyfile, s *ir.Schema) {
	f.line("def bench_main(w: str, reps: int, data: bytes) -> int:")
	f.line("    # The subtraction assumes the two runs differ only in `reps`. A GC pause")
	f.line("    # in one and not the other would land wholly in the delta.")
	f.line("    gc.disable()")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		low := strings.ToLower(m.Name)
		f.line("    if w in ('encode_%s', 'decode_%s'):", low, low)
		f.line("        obj = message.%s.from_jsonable(json.loads(data))", mt)
		f.line("        wire = obj.encode()  # setup: the decode input")
		f.line("        sink = 0")
		f.line("        if w == 'encode_%s':", low)
		f.line("            for _ in range(reps):")
		f.line("                sink ^= len(obj.encode())")
		f.line("        else:")
		f.line("            for _ in range(reps):")
		f.line("                sink ^= %s", benchSink(m, "message."+mt+".decode(wire)"))
		f.line("        sys.stderr.write('sink=%%d bytes=%%d\\n' %% (sink, len(wire)))")
		f.line("        sys.stderr.write('wire_hex=%%s\\n' %% wire.hex())")
		f.line("        return 0")
	}
	f.line("    sys.stderr.write('unknown workload: %%s\\n' %% w)")
	f.line("    return 2")
	f.blank()
}

func (g *gen) harness(s *ir.Schema) []byte {
	f := &pyfile{}
	f.line("# Code generated by %s; DO NOT EDIT.", g.banner)
	f.line("import sys, json, gc")
	f.line("import message")
	f.blank()
	f.line("MESSAGES = {")
	for _, m := range s.Messages {
		f.line("    %q: message.%s,", m.Name, exported(m.Name))
	}
	f.line("}")
	f.blank()
	g.emitBench(f, s)
	f.line("def main() -> int:")
	f.line("    if len(sys.argv) < 2:")
	f.line("        sys.stderr.write('usage: harness.py <encode|decode|bench> [Message|workload]\\n')")
	f.line("        return 2")
	f.line("    mode = sys.argv[1]")
	f.line("    name = sys.argv[2] if len(sys.argv) > 2 else %q", defaultMessage(s))
	f.line("    # `bench <workload> <reps>` takes a workload, not a message name.")
	f.line("    if mode == 'bench':")
	f.line("        return bench_main(name, int(sys.argv[3]) if len(sys.argv) > 3 else 1000, sys.stdin.buffer.read())")
	f.line("    cls = MESSAGES.get(name)")
	f.line("    if cls is None:")
	f.line("        sys.stderr.write('unknown message\\n')")
	f.line("        return 2")
	f.line("    data = sys.stdin.buffer.read()")
	f.line("    if mode == 'encode':")
	f.line("        obj = cls.from_jsonable(json.loads(data))")
	f.line("        sys.stdout.buffer.write(obj.encode())")
	f.line("    elif mode == 'decode':")
	f.line("        obj = cls.decode(data)")
	f.line("        sys.stdout.write(json.dumps(obj.to_jsonable()))")
	f.line("        sys.stdout.write('\\n')")
	f.line("    else:")
	f.line("        sys.stderr.write('unknown mode\\n')")
	f.line("        return 2")
	f.line("    return 0")
	f.blank()
	f.line("if __name__ == '__main__':")
	f.line("    sys.exit(main())")
	return f.bytes()
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}

