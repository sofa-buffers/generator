package docs

import (
	"os"
	"regexp"
	"strings"
	"testing"

	"github.com/sofa-buffers/generator/internal/analysis"
	"github.com/sofa-buffers/generator/internal/ir"
	"github.com/sofa-buffers/generator/internal/model"
	"github.com/sofa-buffers/generator/internal/parser"
)

func schema(t *testing.T, src string) *ir.Schema {
	t.Helper()
	doc, err := parser.Parse([]byte(src), "t.yaml")
	if err != nil {
		t.Fatal(err)
	}
	resolved, _ := doc.Resolve()
	if errs := parser.Validate(resolved); errs != nil {
		t.Fatalf("invalid: %v", errs)
	}
	s, err := model.Build(doc)
	if err != nil {
		t.Fatal(err)
	}
	if err := analysis.Analyze(s); err != nil {
		t.Fatal(err)
	}
	return s
}

func schemaFile(t *testing.T, path string) *ir.Schema {
	t.Helper()
	b, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	return schema(t, string(b))
}

func genDocs(t *testing.T, s *ir.Schema, cfg map[string]any) string {
	t.Helper()
	files, err := (&Backend{}).Generate(s, cfg)
	if err != nil {
		t.Fatalf("generate: %v", err)
	}
	if len(files) != 1 || files[0].Path != "message.html" {
		t.Fatalf("expected exactly [message.html], got %v", files)
	}
	return string(files[0].Content)
}

func TestDocsStructural(t *testing.T) {
	page := genDocs(t, schemaFile(t, "../../examples/messages/example.yaml"), map[string]any{})
	for _, want := range []string{
		"<!DOCTYPE html>",
		"<!-- Code generated by sofabgen; DO NOT EDIT. -->",
		`id="msg-myfirstmessage"`,
		// composite fields link to their named-type section
		`href="#type-myfirstmessage_someenum"`,
		`id="type-myfirstmessage_someenum"`,
		// enum default resolved to its constant name
		"BLUE (2)",
		// type column rendering: maxlen, fixed arrays, array-of-array dims
		"string (maxlen 50)",
		"u32[4]",
		"u32[2][4]",
		// dynamic array (somemap has no count)
		"[]",
		"deprecated",
		// union field default (default_id) resolved to its option name
		"option1 (id 0)",
	} {
		if !strings.Contains(page, want) {
			t.Errorf("message.html missing %q", want)
		}
	}
}

// TestDocsLinksResolve: every intra-page href="#..." has a matching id="...".
func TestDocsLinksResolve(t *testing.T) {
	page := genDocs(t, schemaFile(t, "../../examples/messages/example.yaml"), map[string]any{})
	ids := map[string]bool{}
	for _, m := range regexp.MustCompile(`id="([^"]+)"`).FindAllStringSubmatch(page, -1) {
		ids[m[1]] = true
	}
	for _, m := range regexp.MustCompile(`href="#([^"]+)"`).FindAllStringSubmatch(page, -1) {
		if !ids[m[1]] {
			t.Errorf("dangling link #%s", m[1])
		}
	}
}

// TestDocsEscaping: definition text is HTML-escaped, never injected raw.
func TestDocsEscaping(t *testing.T) {
	src := "version: 1\nmessages:\n  m:\n    payload:\n      a: {id: 0, type: u8, description: \"<script>alert(1)</script> & <b>bold</b>\"}\n"
	page := genDocs(t, schema(t, src), map[string]any{})
	if strings.Contains(page, "<script>alert(1)</script>") {
		t.Error("description injected unescaped")
	}
	if !strings.Contains(page, "&lt;script&gt;") {
		t.Error("escaped description missing")
	}
}

func TestDocsLicenseHeader(t *testing.T) {
	s := schema(t, "version: 1\nmessages:\n  m:\n    payload:\n      a: {id: 0, type: u8}\n")
	if page := genDocs(t, s, map[string]any{"license": "MIT"}); !strings.Contains(page, "<!-- SPDX-License-Identifier: MIT -->") {
		t.Error("license set: SPDX comment missing")
	}
	if page := genDocs(t, s, map[string]any{}); strings.Contains(page, "SPDX-License-Identifier") {
		t.Error("no license: SPDX comment must be absent")
	}
}

func TestDocsUnsupportedFormat(t *testing.T) {
	s := schema(t, "version: 1\nmessages:\n  m:\n    payload:\n      a: {id: 0, type: u8}\n")
	if _, err := (&Backend{}).Generate(s, map[string]any{"format": "markdown"}); err == nil {
		t.Fatal("format markdown should be rejected")
	}
}
