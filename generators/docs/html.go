// page is the fully pre-computed view model handed to the HTML template. All
// linking/formatting decisions are made in Go; the template only lays out.
type page struct {
	Banner   string
	License  string // SPDX id, "" for none
	Version  int
	Messages []*section
	Types    []*section
}

// section documents one message or one named type. Exactly one of
// Fields/Consts/Flags is populated, matching the Category badge.
type section struct {
	Title   string
	Anchor  string
	Badge   string // "message" | "struct" | "union" | "enum" | "bitfield"
	Summary string
	Note    string // extra line under the summary (e.g. the union default option)
	// HasUnit is PAGE-wide (any field anywhere carries a unit), not per
	// section, so every field table shares the same column grid — mixed
	// layouts would make the reader's eye re-find the columns per table.
	HasUnit bool

	Fields []*fieldRow
	Consts []*constRow
	Flags  []*flagRow
}

func (p *page) sections() []*section {
	return append(append([]*section{}, p.Messages...), p.Types...)
}

type fieldRow struct {
	ID          int64
	Name        string
	Type        template.HTML // pre-escaped; may contain a link to a named type
	Default     string
	Unit        string
	Description string
	Deprecated  bool
}

type constRow struct {
	Name        string
	Value       int64
	Description string
}

type flagRow struct {
	Name        string
	Pos         int64
	Default     string
	Description string
}

func (g *gen) htmlPage() ([]byte, error) {
	p := &page{Banner: g.banner, License: g.license, Version: g.schema.Version}
	for _, m := range g.schema.Messages {
		p.Messages = append(p.Messages, g.messageSection(m))
	}
	for _, key := range g.schema.NamedOrder {
		p.Types = append(p.Types, g.typeSection(g.schema.Named[key]))
	}
	// Promote HasUnit to a page-wide flag (see the section comment).
	hasUnit := false
	for _, s := range p.sections() {
		hasUnit = hasUnit || s.HasUnit
	}
	for _, s := range p.sections() {
		s.HasUnit = hasUnit
	}
	var buf bytes.Buffer
	// html/template strips comments from template output, so the generated-by
	// and SPDX header comments are written directly (banner/license are config
	// strings, still escaped here for safety).
	fmt.Fprintf(&buf, "<!-- Code generated by %s; DO NOT EDIT. -->\n", html.EscapeString(g.banner))
	if g.license != "" {
		fmt.Fprintf(&buf, "<!-- SPDX-License-Identifier: %s -->\n", html.EscapeString(g.license))
	}
	if err := pageTmpl.Execute(&buf, p); err != nil {
		return nil, fmt.Errorf("docs: rendering html: %w", err)
	}
	return buf.Bytes(), nil
}

func (g *gen) messageSection(m *ir.Message) *section {
	s := &section{Title: m.Name, Anchor: "msg-" + m.Name, Badge: "message", Summary: strings.TrimSpace(m.Summary)}
	g.fillFields(s, m.Fields)
	return s
}

func (g *gen) typeSection(nt *ir.NamedType) *section {
	s := &section{Title: pascal(nt.Key), Anchor: anchor(nt.Key), Summary: strings.TrimSpace(nt.Summary)}
	switch nt.Category {
	case ir.CatStruct:
		s.Badge = "struct"
		g.fillFields(s, nt.Fields)
	case ir.CatUnion:
		s.Badge = "union"
		g.fillFields(s, nt.Fields)
		s.Note = "Exactly one option is set at a time."
	case ir.CatEnum:
		s.Badge = "enum"
		for _, c := range nt.Consts {
			s.Consts = append(s.Consts, &constRow{Name: c.Name, Value: c.Value, Description: c.Description})
		}
	case ir.CatBitfield:
		s.Badge = "bitfield"
		for _, fl := range nt.Flags {
			r := &flagRow{Name: fl.Name, Pos: fl.Pos, Description: fl.Description}
			if fl.HasDefault {
				r.Default = fmt.Sprint(fl.Default)
			}
			s.Flags = append(s.Flags, r)
		}
	}
	return s
}

func (g *gen) fillFields(s *section, fields []*ir.Field) {
	for _, f := range fields {
		row := &fieldRow{
			ID:          f.ID,
			Name:        f.Name,
			Type:        g.typeHTML(f),
			Default:     g.defaultText(f),
			Unit:        f.Unit,
			Description: f.Description,
			Deprecated:  f.Deprecated,
		}
		if f.Unit != "" {
			s.HasUnit = true
		}
		s.Fields = append(s.Fields, row)
	}
}

// typeHTML renders a field's type, linking composite elements to their named
// type's section. It returns pre-escaped HTML (all names pass through
// html.EscapeString before concatenation).
func (g *gen) typeHTML(f *ir.Field) template.HTML {
	switch f.Kind {
	case ir.KindArray:
		return g.arrayHTML(f)
	case ir.KindEnum, ir.KindBitfield, ir.KindStruct, ir.KindUnion:
		return refHTML(f.Kind, f.Ref)
	case ir.KindString, ir.KindBlob:
		if f.HasMaxlen {
			return template.HTML(html.EscapeString(fmt.Sprintf("%s (maxlen %d)", f.Kind, f.Maxlen)))
		}
	}
	return template.HTML(html.EscapeString(f.Kind.String()))
}

// arrayHTML renders an array type as <base>[outer][inner]…, e.g. u32[4],
// string[5] (maxlen 16), Point[3] (linked), u32[2][4] for array-of-array. An
// omitted count (dynamic array) renders as [].
func (g *gen) arrayHTML(f *ir.Field) template.HTML {
	var dims []string
	elem, ref := f.Elem, f.ElemRef
	hasCount, count := f.HasCount, f.Count
	maxHas, maxLen := f.ElemMaxHas, f.ElemMax
	items := f.ElemItems
	for {
		if hasCount {
			dims = append(dims, fmt.Sprintf("[%d]", count))
		} else {
			dims = append(dims, "[]")
		}
		if elem != ir.KindArray {
			break
		}
		elem, ref = items.Elem, items.ElemRef
		hasCount, count = items.HasCount, items.Count
		maxHas, maxLen = items.ElemMaxHas, items.ElemMax
		items = items.ElemItems
	}
	var base string
	switch elem {
	case ir.KindEnum, ir.KindBitfield, ir.KindStruct, ir.KindUnion:
		base = string(refHTML(elem, ref))
	default:
		base = html.EscapeString(elem.String())
	}
	out := base + strings.Join(dims, "")
	if maxHas {
		out += html.EscapeString(fmt.Sprintf(" (maxlen %d)", maxLen))
	}
	return template.HTML(out)
}

// refHTML renders a composite type as its kind word plus a link to the named
// type's section, e.g. `struct <a href="#type-struct-Point">Point</a>`.
func refHTML(k ir.Kind, ref *ir.TypeRef) template.HTML {
	return template.HTML(fmt.Sprintf(`%s <a href="#%s">%s</a>`,
		html.EscapeString(k.String()), anchor(ref.Key), html.EscapeString(pascal(ref.Key))))
}

// defaultText renders a field's default for the Default column. Values pass
// through as authored in the definition (strings quoted, blob defaults stay
// base64); an enum default is resolved to its constant name when possible.
func (g *gen) defaultText(f *ir.Field) string {
	if f.Default == nil {
		return ""
	}
	if f.Ref != nil && f.Ref.Target != nil {
		if v, ok := asInt64(f.Default); ok {
			switch f.Kind {
			case ir.KindEnum:
				for _, c := range f.Ref.Target.Consts {
					if c.Value == v {
						return fmt.Sprintf("%s (%d)", c.Name, v)
					}
				}
			case ir.KindUnion: // Default carries the default_id -> name the option
				for _, o := range f.Ref.Target.Fields {
					if o.ID == v {
						return fmt.Sprintf("%s (id %d)", o.Name, v)
					}
				}
			}
		}
	}
	return formatValue(f.Default)
}

func formatValue(v any) string {
	switch t := v.(type) {
	case string:
		return fmt.Sprintf("%q", t)
	case []byte:
		return fmt.Sprintf("%q", string(t))
	case []any:
		parts := make([]string, len(t))
		for i, e := range t {
			parts[i] = formatValue(e)
		}
		return "[" + strings.Join(parts, ", ") + "]"
	default:
		return fmt.Sprint(t)
	}
}

func asInt64(v any) (int64, bool) {
	switch t := v.(type) {
	case int:
		return int64(t), true
	case int64:
		return t, true
	case uint64:
		return int64(t), true
	case float64:
		return int64(t), true
	}
	return 0, false
}

// pascal turns a named-type graph key ("struct/Point", inline synthetic
// "myfirstmessage_someenum") into the PascalCase display name the code
// backends use for the same type, so docs and generated identifiers agree.
func pascal(key string) string {
	parts := strings.FieldsFunc(key, func(r rune) bool { return r == '/' || r == '_' })
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		b.WriteString(p[1:])
	}
	return b.String()
}

// anchor is the fragment id of a named type's section ("/" is not fragment-safe).
func anchor(key string) string {
	return "type-" + strings.ReplaceAll(key, "/", "-")
}

