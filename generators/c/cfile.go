package c

import (
	"fmt"
	"strings"
)

// cfile is the small Builder for a single C source file: it manages line output
// and indentation-free intent-level writes so visitors never concatenate raw
// cross-file strings (PLAN §8.4). Formatting (banner, blank lines) lives here,
// content in the gen visitor.
type cfile struct{ b strings.Builder }

func (f *cfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}

func (f *cfile) blank() { f.b.WriteByte('\n') }

func (f *cfile) bytes() []byte { return []byte(f.b.String()) }

// banner writes the @generated DO-NOT-EDIT header (PLAN §5.1). A non-empty
// license adds an SPDX line.
func (f *cfile) banner(tool, license, file, msg string) {
	f.line("/*!")
	f.line(" * @file %s", file)
	f.line(" * @brief SofaBuffers generated types for %q — DO NOT EDIT.", msg)
	f.line(" *")
	f.line(" * @generated by %s. Regenerate instead of editing; manual changes are lost.", tool)
	if license != "" {
		f.line(" * SPDX-License-Identifier: %s", license)
	}
	f.line(" */")
	f.blank()
}

// doc writes a Doxygen /*! ... */ one-liner (or multi-line) doc comment.
func (f *cfile) doc(format string, args ...any) {
	text := fmt.Sprintf(format, args...)
	lines := strings.Split(strings.TrimRight(text, "\n"), "\n")
	if len(lines) == 1 {
		f.line("/*! %s */", lines[0])
		return
	}
	f.line("/*!")
	for _, l := range lines {
		f.line(" * %s", l)
	}
	f.line(" */")
}
