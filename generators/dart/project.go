// projectFiles scaffolds a buildable Dart package with a JSON encode/decode
// harness over corelib-dart (the shape the conformance harness drives).
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	return []generator.File{
		{Path: "pubspec.yaml", Content: []byte(pubspec)},
		{Path: "bin/harness.dart", Content: g.harness(s)},
		{Path: "README.md", Content: []byte(dartReadme)},
	}
}

const dartReadme = "# Generated SofaBuffers Dart project\n\n" +
	"`@generated by sofabgen`. Encode/decode harness over `corelib-dart`.\n\n" +
	"Set the corelib path in `pubspec.yaml` (the `${SOFAB_DART_CORELIB}` placeholder),\n" +
	"then:\n\n" +
	"```sh\ndart pub get\ndart run bin/harness.dart encode <Message> < in.json\n```\n"

const pubspec = `# @generated by sofabgen
name: harness
description: SofaBuffers generated encode/decode harness.
publish_to: none
version: 0.0.0
environment:
  sdk: ^3.4.0
dependencies:
  sofabuffers:
    path: ${SOFAB_DART_CORELIB}
`

func (g *gen) harness(s *ir.Schema) []byte {
	f := &dfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	f.line("// ignore_for_file: unused_local_variable, deprecated_member_use_from_same_package")
	f.line("import 'dart:convert';")
	f.line("import 'dart:io';")
	f.line("import 'dart:typed_data';")
	f.line("import 'package:harness/message.dart';")
	f.line("import 'package:sofabuffers/sofabuffers.dart' as sofab;")
	f.blank()

	// Per-object JSON codecs (message + named struct/union).
	for _, key := range s.NamedOrder {
		nt := s.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitJSONCodec(f, g.typeName(key), nt.Fields)
		}
	}
	for _, m := range s.Messages {
		g.emitJSONCodec(f, exported(m.Name), m.Fields)
	}

	g.emitBench(f, s)

	// Dart ignores an int returned from main; the process exit code is set only
	// via exit()/exitCode, which the conformance harness relies on to distinguish
	// a rejected (INVALID/INCOMPLETE/limitExceeded) decode from a clean one.
	f.line("void main(List<String> args) {")
	f.line("  if (args.isEmpty) {")
	f.line("    stderr.writeln('usage: harness <encode|decode|trydecode|recode|bench> [Message|workload]');")
	f.line("    exit(2);")
	f.line("  }")
	f.line("  final mode = args[0];")
	f.line("  final name = args.length > 1 ? args[1] : %s;", dartStringLit(defaultMessage(s)))
	f.line("  final input = _readStdin();")
	f.line("  if (mode == 'bench') {")
	f.line("    exit(_benchMain(name, args.length > 2 ? int.parse(args[2]) : 1000, input));")
	f.line("  }")
	f.line("  switch (name) {")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		f.line("    case %s:", dartStringLit(m.Name))
		f.line("      if (mode == 'encode') {")
		f.line("        final obj = _fromJson%s(jsonDecode(utf8.decode(input)) as Map<String, dynamic>);", mt)
		f.line("        stdout.add(obj.encode());")
		f.line("      } else if (mode == 'decode') {")
		f.line("        final obj = %s();", mt)
		f.line("        final st = %s.tryDecode(input, obj);", mt)
		f.line("        if (st != sofab.DecodeStatus.complete) {")
		f.line("          stderr.writeln('decode failed: ${st.name}');")
		f.line("          exit(1);")
		f.line("        }")
		f.line("        stdout.writeln(jsonEncode(_toJson%s(obj)));", mt)
		f.line("      } else if (mode == 'trydecode') {")
		f.line("        final obj = %s();", mt)
		f.line("        final st = %s.tryDecode(input, obj);", mt)
		f.line("        stdout.writeln(st.name.toUpperCase());")
		f.line("        stdout.writeln(jsonEncode(_toJson%s(obj)));", mt)
		f.line("      } else if (mode == 'recode') {")
		// Wire -> object -> wire, no JSON in the loop: the byte-exact re-encode path
		// that must preserve an fp32 signaling NaN's raw bits (issue #226).
		f.line("        final obj = %s();", mt)
		f.line("        final st = %s.tryDecode(input, obj);", mt)
		f.line("        if (st != sofab.DecodeStatus.complete) {")
		f.line("          stderr.writeln('decode failed: ${st.name}');")
		f.line("          exit(1);")
		f.line("        }")
		f.line("        stdout.add(obj.encode());")
		f.line("      } else {")
		f.line("        stderr.writeln('unknown mode');")
		f.line("        exit(2);")
		f.line("      }")
		f.line("      return;")
	}
	f.line("    default:")
	f.line("      stderr.writeln('unknown message');")
	f.line("      exit(2);")
	f.line("  }")
	f.line("}")
	f.blank()
	f.line("Uint8List _readStdin() {")
	f.line("  final b = BytesBuilder(copy: true);")
	f.line("  var byte = stdin.readByteSync();")
	f.line("  while (byte != -1) {")
	f.line("    b.addByte(byte);")
	f.line("    byte = stdin.readByteSync();")
	f.line("  }")
	f.line("  return b.toBytes();")
	f.line("}")
	return f.bytes()
}

// emitJSONCodec emits `_toJson<T>` / `_fromJson<T>` for one object type. JSON
// field keys use the ORIGINAL schema name (wire/JSON name is unaffected by
// identifier mangling); member access uses the mangled Dart identifier. 64-bit
// unsigned values are carried as JSON strings — Dart's `int` is signed 64-bit
// and jsonDecode reads a large number as a lossy double (verified), so a u64
// above 2^63 must round-trip as a decimal string.
func (g *gen) emitJSONCodec(f *dfile, typeName string, fields []*ir.Field) {
	f.line("Map<String, dynamic> _toJson%s(%s m) {", typeName, typeName)
	f.line("  return <String, dynamic>{")
	for _, fld := range fields {
		f.line("    %s: %s,", dartStringLit(fld.Name), g.jsonTo(fld, "m."+dartIdent(fld.Name)))
	}
	f.line("  };")
	f.line("}")
	f.blank()
	f.line("%s _fromJson%s(Map<String, dynamic> j) {", typeName, typeName)
	f.line("  final m = %s();", typeName)
	for _, fld := range fields {
		key := dartStringLit(fld.Name)
		f.line("  if (j.containsKey(%s)) { m.%s = %s; }", key, dartIdent(fld.Name), g.jsonFrom(fld, "j["+key+"]"))
	}
	f.line("  return m;")
	f.line("}")
	f.blank()
}

// jsonTo is the Dart expression converting field `acc` to its JSON value.
func (g *gen) jsonTo(fld *ir.Field, acc string) string {
	switch fld.Kind {
	case ir.KindU64:
		return u64ToJSON(acc)
	case ir.KindU8, ir.KindU16, ir.KindU32,
		ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64,
		ir.KindEnum, ir.KindBitfield, ir.KindFP32, ir.KindFP64, ir.KindBool, ir.KindString:
		return acc
	case ir.KindBlob:
		return acc + ".toList()"
	case ir.KindStruct, ir.KindUnion:
		return fmt.Sprintf("_toJson%s(%s)", g.typeName(fld.Ref.Key), acc)
	case ir.KindArray:
		return g.arrayElemToJSON(fld.Elem, fld.ElemRef, fld.ElemItems, acc)
	}
	return acc
}

func (g *gen) arrayElemToJSON(elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, acc string) string {
	switch elem {
	case ir.KindU64:
		return fmt.Sprintf("[for (final _x in %s) %s]", acc, u64ToJSON("_x"))
	case ir.KindBlob:
		return fmt.Sprintf("[for (final _x in %s) _x.toList()]", acc)
	case ir.KindStruct, ir.KindUnion:
		return fmt.Sprintf("[for (final _x in %s) _toJson%s(_x)]", acc, g.typeName(ref.Key))
	case ir.KindArray:
		return fmt.Sprintf("[for (final _x in %s) %s]", acc, g.arrayElemToJSON(items.Elem, items.ElemRef, items.ElemItems, "_x"))
	default: // int/enum/bitfield/bool/fp/string pass through
		return acc
	}
}

// jsonFrom is the Dart expression building field `fld` from JSON accessor `jx`.
func (g *gen) jsonFrom(fld *ir.Field, jx string) string {
	switch fld.Kind {
	case ir.KindU64:
		return u64FromJSON(jx)
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum, ir.KindBitfield:
		return fmt.Sprintf("(%s as num).toInt()", jx)
	case ir.KindFP32, ir.KindFP64:
		return fmt.Sprintf("(%s as num).toDouble()", jx)
	case ir.KindBool:
		return fmt.Sprintf("%s as bool", jx)
	case ir.KindString:
		return fmt.Sprintf("%s as String", jx)
	case ir.KindBlob:
		return fmt.Sprintf("Uint8List.fromList([for (final _b in (%s as List)) (_b as num).toInt()])", jx)
	case ir.KindStruct, ir.KindUnion:
		return fmt.Sprintf("_fromJson%s(%s as Map<String, dynamic>)", g.typeName(fld.Ref.Key), jx)
	case ir.KindArray:
		et := g.dartArrayElemType(fld.Elem, fld.ElemRef, fld.ElemItems)
		return fmt.Sprintf("<%s>[for (final _x in (%s as List)) %s]", et, jx, g.arrayElemFromJSON(fld.Elem, fld.ElemRef, fld.ElemItems, "_x"))
	}
	return jx
}

func (g *gen) arrayElemFromJSON(elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, jx string) string {
	switch elem {
	case ir.KindU64:
		return u64FromJSON(jx)
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum, ir.KindBitfield:
		return fmt.Sprintf("(%s as num).toInt()", jx)
	case ir.KindFP32, ir.KindFP64:
		return fmt.Sprintf("(%s as num).toDouble()", jx)
	case ir.KindBool:
		return fmt.Sprintf("%s as bool", jx)
	case ir.KindString:
		return fmt.Sprintf("%s as String", jx)
	case ir.KindBlob:
		return fmt.Sprintf("Uint8List.fromList([for (final _b in (%s as List)) (_b as num).toInt()])", jx)
	case ir.KindStruct, ir.KindUnion:
		return fmt.Sprintf("_fromJson%s(%s as Map<String, dynamic>)", g.typeName(ref.Key), jx)
	case ir.KindArray:
		inner := g.dartArrayElemType(items.Elem, items.ElemRef, items.ElemItems)
		return fmt.Sprintf("<%s>[for (final _y in (%s as List)) %s]", inner, jx, g.arrayElemFromJSON(items.Elem, items.ElemRef, items.ElemItems, "_y"))
	}
	return jx
}

// u64ToJSON / u64FromJSON carry a 64-bit unsigned value as an unsigned-decimal
// JSON string (see emitJSONCodec).
func u64ToJSON(acc string) string {
	return fmt.Sprintf("BigInt.from(%s).toUnsigned(64).toString()", acc)
}

func u64FromJSON(jx string) string {
	// toSigned(64) yields a BigInt that fits Dart's signed 64-bit int exactly, so
	// toInt() is lossless (unlike a plain toInt() which clamps).
	return fmt.Sprintf("BigInt.parse(%s as String).toSigned(64).toInt()", jx)
}

// emitBench emits the `bench <workload> <reps>` entry point (tests/bench,
// ARCHITECTURE §15), measured by the `subtract` method: the Dart VM JITs the hot
// path, so there is no native symbol to toggle on. A fixed warmup (independent of
// reps) is JITted before the measured loop, so the two rep counts differ only in
// the measured op count and the startup/JIT cost cancels in the subtraction.
func (g *gen) emitBench(f *dfile, s *ir.Schema) {
	f.line("int _benchSink = 0;")
	f.blank()
	f.line("int _benchMain(String w, int reps, Uint8List input) {")
	f.line("  final warmup = int.tryParse(Platform.environment['SOFAB_BENCH_WARMUP'] ?? '') ?? 5000;")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		low := strings.ToLower(m.Name)
		f.line("  if (w == 'encode_%s' || w == 'decode_%s') {", low, low)
		f.line("    final obj = _fromJson%s(jsonDecode(utf8.decode(input)) as Map<String, dynamic>);", mt)
		f.line("    final wire = obj.encode();")
		f.line("    final enc = w == 'encode_%s';", low)
		f.line("    for (var i = 0; i < warmup; i++) { _benchOp%s(enc, obj, wire); }", mt)
		f.line("    for (var i = 0; i < reps; i++) { _benchOp%s(enc, obj, wire); }", mt)
		f.line("    stderr.writeln('sink=$_benchSink bytes=${wire.length}');")
		f.line("    return 0;")
		f.line("  }")
	}
	f.line("  stderr.writeln('unknown workload: $w');")
	f.line("  return 2;")
	f.line("}")
	f.blank()
	for _, m := range s.Messages {
		mt := exported(m.Name)
		sink := benchSinkField(m)
		f.line("void _benchOp%s(bool enc, %s obj, Uint8List wire) {", mt, mt)
		f.line("  if (enc) {")
		f.line("    _benchSink ^= obj.encode().length;")
		f.line("  } else {")
		if sink != "" {
			f.line("    _benchSink ^= %s.decode(wire).%s;", mt, sink)
		} else {
			f.line("    _benchSink ^= %s.decode(wire).hashCode;", mt)
		}
		f.line("  }")
		f.line("}")
		f.blank()
	}
}

// benchSinkField names one cheap integer scalar folded into the bench loop so the
// decode cannot be elided.
func benchSinkField(m *ir.Message) string {
	for _, fld := range m.Fields {
		switch fld.Kind {
		case ir.KindU8, ir.KindU16, ir.KindU32,
			ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
			return dartIdent(fld.Name)
		}
	}
	return ""
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}
