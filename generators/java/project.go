// benchSinkField names one cheap integer scalar of m, folded in the bench loop so
// the decode cannot be elided. It runs inside the measured loop, so it must stay
// cheap -- Json.to() would be counted as decode cost.
func benchSinkField(m *ir.Message) string {
	for _, f := range m.Fields {
		switch f.Kind {
		case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64,
			ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
			return f.Name
		}
	}
	return ""
}

// emitBench emits the `bench <workload> <reps>` entry point (tests/bench,
// ARCHITECTURE §15).
//
// The JVM JIT-compiles the hot path at runtime, so there is no native symbol to
// --toggle-collect on. This is the `subtract` method: tests/bench runs the same
// workload at two rep counts and subtracts the totals,
//
//	Ir/op = ( Ir(R2) - Ir(R1) ) / ( R2 - R1 )
//
// which cancels JVM startup, class loading, JIT compilation and setup exactly.
//
// That only holds if the two runs differ in NOTHING but the rep count, which needs
// both halves of the contract:
//   - here: a FIXED warmup, independent of reps, so the hot methods reach their
//     final tier BEFORE the measured loop and every measured op runs at steady cost;
//   - in tests/bench/lang/java.sh: -XX:-TieredCompilation (one tier, no mid-run
//     transition), -XX:+UseEpsilonGC with a fixed heap (no GC at all), and
//     -XX:hashCode=2 (identity hashes are otherwise seeded per-run).
//
// Ported from corelib-java/src/main/java/org/sofabuffers/sofab/bench/Callgrind.java.
func (g *gen) emitBench(f *jfile, s *ir.Schema) {
	f.line("    // Fixed warmup ops per run, independent of `reps`, so it cancels in the")
	f.line("    // subtraction while leaving the measured ops at steady, fully-JITted cost.")
	f.line("    private static final int WARMUP = Integer.getInteger(\"sofab.warmup\", 5000);")
	f.line("    private static long benchSink = 0;")
	f.blank()
	f.line("    static int benchMain(String w, int reps, byte[] input) throws Exception {")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		low := strings.ToLower(m.Name)
		f.line("        if (w.equals(\"encode_%s\") || w.equals(\"decode_%s\")) {", low, low)
		f.line("            JsonObject j = JsonParser.parseString(new String(input, StandardCharsets.UTF_8)).getAsJsonObject();")
		f.line("            %s obj = new %s(); Json.from(j, obj);", mt, mt)
		f.line("            byte[] wire = obj.encode(); // setup: the decode input")
		f.line("            boolean enc = w.equals(\"encode_%s\");", low)
		f.line("            for (int i = 0; i < WARMUP; i++) benchOp_%s(enc, obj, wire);", low)
		f.line("            for (int i = 0; i < reps; i++) benchOp_%s(enc, obj, wire);", low)
		f.line("            System.err.println(\"sink=\" + benchSink + \" bytes=\" + wire.length);")
		f.line("            StringBuilder hx = new StringBuilder();")
		f.line("            for (byte b : wire) hx.append(String.format(\"%%02x\", b));")
		f.line("            System.err.println(\"wire_hex=\" + hx);")
		f.line("            return 0;")
		f.line("        }")
	}
	f.line("        System.err.println(\"unknown workload: \" + w);")
	f.line("        return 2;")
	f.line("    }")
	f.blank()

	for _, m := range s.Messages {
		mt := exported(m.Name)
		low := strings.ToLower(m.Name)
		sink := benchSinkField(m)
		f.line("    private static void benchOp_%s(boolean enc, %s obj, byte[] wire) {", low, mt)
		f.line("        if (enc) {")
		f.line("            benchSink ^= obj.encode().length;")
		f.line("        } else {")
		if sink != "" {
			f.line("            benchSink ^= %s.decode(wire).%s;", mt, sink)
		} else {
			f.line("            benchSink ^= %s.decode(wire).hashCode();", mt)
		}
		f.line("        }")
		f.line("    }")
		f.blank()
	}
}

func (g *gen) mainHarness(s *ir.Schema) []byte {
	f := &jfile{}
	g.header(f)
	f.line("package %s;", g.pkg)
	f.line("import com.google.gson.*;")
	f.line("import java.io.*;")
	f.line("import java.nio.charset.StandardCharsets;")
	f.blank()
	f.line("public class Main {")
	g.emitBench(f, s)
	f.line("    public static void main(String[] args) throws Exception {")
	f.line("        String mode = args.length > 0 ? args[0] : \"\";")
	f.line("        String name = args.length > 1 ? args[1] : %q;", defaultMessage(s))
	f.line("        ByteArrayOutputStream bos = new ByteArrayOutputStream();")
	f.line("        byte[] tmp = new byte[8192]; int n; while ((n = System.in.read(tmp)) > 0) bos.write(tmp, 0, n);")
	f.line("        byte[] input = bos.toByteArray();")
	f.line("        // `bench <workload> <reps>` takes a workload, not a message name.")
	f.line("        if (mode.equals(\"bench\")) {")
	f.line("            System.exit(benchMain(name, args.length > 2 ? Integer.parseInt(args[2]) : 1000, input));")
	f.line("        }")
	f.line("        switch (name) {")
	for _, m := range s.Messages {
		mt := exported(m.Name)
		f.line("        case %q: {", m.Name)
		f.line("            if (mode.equals(\"encode\")) {")
		f.line("                JsonObject j = JsonParser.parseString(new String(input, StandardCharsets.UTF_8)).getAsJsonObject();")
		f.line("                %s obj = new %s(); Json.from(j, obj);", mt, mt)
		f.line("                System.out.write(obj.encode());")
		f.line("            } else if (mode.equals(\"decode\")) {")
		f.line("                %s obj = %s.decode(input);", mt, mt)
		f.line("                StringBuilder sb = new StringBuilder(); Json.to(obj, sb);")
		f.line("                System.out.write(sb.toString().getBytes(StandardCharsets.UTF_8)); System.out.write('\\n');")
		f.line("            } else if (mode.equals(\"trydecode\")) {")
		f.line("                %s obj = new %s();", mt, mt)
		f.line("                org.sofabuffers.sofab.DecodeStatus st = %s.tryDecode(input, obj);", mt)
		f.line("                System.out.write((st.name() + \"\\n\").getBytes(StandardCharsets.UTF_8));")
		f.line("                StringBuilder sb = new StringBuilder(); Json.to(obj, sb);")
		f.line("                System.out.write(sb.toString().getBytes(StandardCharsets.UTF_8)); System.out.write('\\n');")
		f.line("            } else { System.err.println(\"unknown mode\"); System.exit(2); }")
		f.line("            break;")
		f.line("        }")
	}
	f.line("        default: System.err.println(\"unknown message\"); System.exit(2);")
	f.line("        }")
	f.line("        System.out.flush();")
	f.line("    }")
	f.line("}")
	return f.bytes()
}

// jsonHelper emits to/from JSON for every object (Gson tree in, hand-written
// out). u64 fields are exact via unsigned string handling.
func (g *gen) jsonHelper(s *ir.Schema) []byte {
	f := &jfile{}
	g.header(f)
	f.line("package %s;", g.pkg)
	f.line("import com.google.gson.*;")
	f.line("import java.util.*;")
	f.blank()
	f.line("final class Json {")

	emitted := map[string]bool{}
	for _, m := range s.Messages {
		for _, key := range g.reachable(m) {
			nt := s.Named[key]
			if (nt.Category == ir.CatStruct || nt.Category == ir.CatUnion) && !emitted[key] {
				emitted[key] = true
				g.emitJSONFns(f, g.typeName(key), nt.Fields)
			}
		}
		g.emitJSONFns(f, exported(m.Name), m.Fields)
	}

	f.line("    static void str(StringBuilder b, String s) {")
	f.line("        b.append('\"');")
	f.line("        for (int i = 0; i < s.length(); i++) { char c = s.charAt(i);")
	f.line("            if (c == '\"' || c == '\\\\') b.append('\\\\').append(c);")
	f.line("            else if (c == '\\n') b.append(\"\\\\n\"); else if (c == '\\t') b.append(\"\\\\t\");")
	f.line("            else if (c < 0x20) b.append(String.format(\"\\\\u%%04x\", (int)c)); else b.append(c); }")
	f.line("        b.append('\"');")
	f.line("    }")
	f.line("    static void bytes(StringBuilder b, byte[] a) {")
	f.line("        b.append('['); for (int i = 0; i < a.length; i++) { if (i>0) b.append(','); b.append(a[i] & 0xFF); } b.append(']');")
	f.line("    }")
	f.line("    static byte[] toBytes(JsonArray a) { byte[] r = new byte[a.size()]; for (int i=0;i<r.length;i++) r[i]=(byte)a.get(i).getAsInt(); return r; }")
	f.line("}")
	return f.bytes()
}

func (g *gen) emitJSONFns(f *jfile, typeName string, fields []*ir.Field) {
	// to
	f.line("    static void to(%s o, StringBuilder b) {", typeName)
	f.line("        b.append('{');")
	for i, fld := range fields {
		if i > 0 {
			f.line("        b.append(',');")
		}
		f.line("        b.append(\"\\\"%s\\\":\");", fld.Name)
		g.emitTo(f, fld)
	}
	f.line("        b.append('}');")
	f.line("    }")
	// from
	f.line("    static void from(JsonObject j, %s o) {", typeName)
	f.line("        JsonElement e;")
	for _, fld := range fields {
		f.line("        e = j.get(%q); if (e != null && !e.isJsonNull()) {", fld.Name)
		g.emitFrom(f, fld)
		f.line("        }")
	}
	f.line("    }")
}

func (g *gen) emitTo(f *jfile, fld *ir.Field) {
	acc := "o." + javaIdent(fld.Name)
	switch fld.Kind {
	case ir.KindU64:
		f.line("        b.append(Long.toUnsignedString(%s));", acc)
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum, ir.KindBitfield:
		f.line("        b.append(%s);", acc)
	case ir.KindBool:
		f.line("        b.append(%s);", acc)
	case ir.KindFP32, ir.KindFP64:
		f.line("        b.append(%s);", acc)
	case ir.KindString:
		f.line("        Json.str(b, %s == null ? \"\" : %s);", acc, acc)
	case ir.KindBlob:
		f.line("        Json.bytes(b, %s == null ? new byte[0] : %s);", acc, acc)
	case ir.KindStruct, ir.KindUnion:
		f.line("        to(%s, b);", acc)
	case ir.KindArray:
		g.emitToArray(f, fld, acc)
	}
}

func (g *gen) emitToArray(f *jfile, fld *ir.Field, acc string) {
	g.jsonToArray(f, "        ", acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, primitiveArrayElem(fld.Elem))
}

// jsonToArray writes val as a JSON array. u* elements print unsigned; struct/union
// recurse via to(); nested arrays recurse; the rest print via append. Loop vars
// are depth-suffixed to nest safely. prim: val is a primitive array (index via
// [i]/length) rather than a boxed List (get(i)/size()).
func (g *gen) jsonToArray(f *jfile, ind, val string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int, prim bool) {
	iv := fmt.Sprintf("_i%d", depth)
	el := fmt.Sprintf("%s.get(%s)", val, iv)
	bound := val + ".size()"
	if prim {
		el = fmt.Sprintf("%s[%s]", val, iv)
		bound = val + ".length"
	}
	f.line("%s{ b.append('['); for (int %s = 0; %s < %s; %s++) { if (%s>0) b.append(',');", ind, iv, iv, bound, iv, iv)
	switch elem {
	case ir.KindString:
		f.line("%s    Json.str(b, %s);", ind, el)
	case ir.KindBlob:
		f.line("%s    Json.bytes(b, %s);", ind, el)
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64:
		f.line("%s    b.append(Long.toUnsignedString(%s));", ind, el)
	case ir.KindStruct, ir.KindUnion:
		f.line("%s    to(%s, b);", ind, el)
	case ir.KindArray:
		g.jsonToArray(f, ind+"    ", el, items.Elem, items.ElemRef, items.ElemItems, depth+1, false)
	default: // i*, enum, bitfield, boolean, fp
		f.line("%s    b.append(%s);", ind, el)
	}
	f.line("%s} b.append(']'); }", ind)
}

func (g *gen) emitFrom(f *jfile, fld *ir.Field) {
	acc := "o." + javaIdent(fld.Name)
	switch fld.Kind {
	case ir.KindU64:
		f.line("            %s = Long.parseUnsignedLong(e.getAsString());", acc)
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum, ir.KindBitfield:
		f.line("            %s = e.getAsLong();", acc)
	case ir.KindBool:
		f.line("            %s = e.getAsBoolean();", acc)
	case ir.KindFP32:
		f.line("            %s = e.getAsFloat();", acc)
	case ir.KindFP64:
		f.line("            %s = e.getAsDouble();", acc)
	case ir.KindString:
		f.line("            %s = e.getAsString();", acc)
	case ir.KindBlob:
		f.line("            %s = Json.toBytes(e.getAsJsonArray());", acc)
	case ir.KindStruct, ir.KindUnion:
		f.line("            from(e.getAsJsonObject(), %s);", acc)
	case ir.KindArray:
		g.emitFromArray(f, fld, acc)
	}
}

func (g *gen) emitFromArray(f *jfile, fld *ir.Field, acc string) {
	g.jsonFromArray(f, "            ", acc, "e.getAsJsonArray()", fld.Elem, fld.ElemRef, fld.ElemItems, 0, primitiveArrayElem(fld.Elem))
}

// jsonFromArray rebuilds target from the JsonArray src, mirroring jsonToArray:
// u64 parses from string, struct/union recurse via from(), nested arrays recurse
// into a fresh List; the rest map to the matching Gson accessor. Loop/temporary
// vars are depth-suffixed to nest safely.
func (g *gen) jsonFromArray(f *jfile, ind, target, src string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int, prim bool) {
	if prim {
		// Primitive array: allocate to the JSON array size and fill by index.
		av := fmt.Sprintf("_a%d", depth)
		kv := fmt.Sprintf("_k%d", depth)
		var getter string
		switch elem {
		case ir.KindU64:
			getter = fmt.Sprintf("Long.parseUnsignedLong(%s.get(%s).getAsString())", av, kv)
		case ir.KindFP32:
			getter = fmt.Sprintf("%s.get(%s).getAsFloat()", av, kv)
		case ir.KindFP64:
			getter = fmt.Sprintf("%s.get(%s).getAsDouble()", av, kv)
		default: // u8/u16/u32, i8..i64, enum, bitfield
			getter = fmt.Sprintf("%s.get(%s).getAsLong()", av, kv)
		}
		f.line("%sJsonArray %s = %s; %s = new %s[%s.size()];", ind, av, src, target, primArrayBase(elem), av)
		f.line("%sfor (int %s = 0; %s < %s.length; %s++) %s[%s] = %s;", ind, kv, kv, target, kv, target, kv, getter)
		return
	}
	ev := fmt.Sprintf("_e%d", depth)
	f.line("%s%s.clear(); for (JsonElement %s : %s) {", ind, target, ev, src)
	switch elem {
	case ir.KindString:
		f.line("%s    %s.add(%s.getAsString());", ind, target, ev)
	case ir.KindBlob:
		f.line("%s    %s.add(Json.toBytes(%s.getAsJsonArray()));", ind, target, ev)
	case ir.KindU64:
		f.line("%s    %s.add(Long.parseUnsignedLong(%s.getAsString()));", ind, target, ev)
	case ir.KindBool:
		f.line("%s    %s.add(%s.getAsBoolean());", ind, target, ev)
	case ir.KindFP32:
		f.line("%s    %s.add(%s.getAsFloat());", ind, target, ev)
	case ir.KindFP64:
		f.line("%s    %s.add(%s.getAsDouble());", ind, target, ev)
	case ir.KindStruct, ir.KindUnion:
		v := fmt.Sprintf("_v%d", depth)
		f.line("%s    %s %s = new %s(); from(%s.getAsJsonObject(), %s); %s.add(%s);", ind, g.typeName(ref.Key), v, g.typeName(ref.Key), ev, v, target, v)
	case ir.KindArray:
		v := fmt.Sprintf("_v%d", depth)
		vt := "List<" + g.javaArrayElemType(items.Elem, items.ElemRef, items.ElemItems) + ">"
		f.line("%s    %s %s = new ArrayList<>();", ind, vt, v)
		g.jsonFromArray(f, ind+"    ", v, ev+".getAsJsonArray()", items.Elem, items.ElemRef, items.ElemItems, depth+1, false)
		f.line("%s    %s.add(%s);", ind, target, v)
	default: // u8/u16/u32, i8..i64, enum, bitfield
		f.line("%s    %s.add(%s.getAsLong());", ind, target, ev)
	}
	f.line("%s}", ind)
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}

