// Package java is the Java throughput backend (PLAN §6.4): classes with
// marshal over OStream and a flat-visitor decode (Visitor) against corelib-java.
// The Visitor is flat (sequenceBegin/end, no child visitors), so decode is a
// (location, id) state machine with a location stack — same as Rust/C#. Java has
// no unsigned ints, so all integers map to long (u64 handled via
// Long.toUnsignedString / parseUnsignedLong in the JSON harness).
package java

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

func init() { generator.Register(&Backend{}) }

// Backend implements generator.Backend for Java.
type Backend struct{}

func (*Backend) Lang() string { return "java" }

func (*Backend) Generate(s *ir.Schema, cfg map[string]any) ([]generator.File, error) {
	g := &gen{schema: s, pkg: cfgString(cfg, "package", "messages"), banner: cfgString(cfg, "tool_banner", "sofabgen"), license: generator.LicenseID(cfg), omit: cfgBool(cfg, "omit_defaults")}
	dir := "src/main/java/" + strings.ReplaceAll(g.pkg, ".", "/") + "/"
	var files []generator.File
	files = append(files, generator.File{Path: dir + "Sbuf.java", Content: g.sbufSupport()})
	for _, m := range s.Messages {
		files = append(files, generator.File{Path: dir + exported(m.Name) + ".java", Content: g.messageFile(m)})
	}
	if cfgString(cfg, "emit", "sources") == "project" {
		files = append(files, g.projectFiles(s, cfg, dir)...)
	}
	return files, nil
}

type gen struct {
	schema  *ir.Schema
	pkg     string
	banner  string
	license string // SPDX id, "" to omit the header line
	omit    bool
}

// header writes the @generated banner and, when a license is set, an SPDX line.
func (g *gen) header(f *jfile) {
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("// SPDX-License-Identifier: %s", g.license)
	}
}

type jfile struct{ b strings.Builder }

func (f *jfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}
func (f *jfile) blank()        { f.b.WriteByte('\n') }
func (f *jfile) bytes() []byte { return []byte(f.b.String()) }

// javadoc writes a Javadoc doc comment for text at the given indent, or nothing
// when text is empty. Single-line text becomes `/** text */`; multi-line text is
// expanded across lines. Any `*/` in the text is neutralised to `* /` so it can
// never close the comment early; UTF-8 passes through byte-for-byte.
func (f *jfile) javadoc(indent, text string) {
	if text == "" {
		return
	}
	text = strings.ReplaceAll(text, "*/", "* /")
	lines := strings.Split(strings.TrimRight(text, "\n"), "\n")
	if len(lines) == 1 {
		f.line("%s/** %s */", indent, lines[0])
		return
	}
	f.line("%s/**", indent)
	for _, ln := range lines {
		if ln == "" {
			f.line("%s *", indent)
		} else {
			f.line("%s * %s", indent, ln)
		}
	}
	f.line("%s */", indent)
}

// fieldDoc is the Javadoc body for a field: its description, with a unit suffix
// appended (or used alone). Empty when the field carries neither.
func fieldDoc(fld *ir.Field) string {
	d := fld.Description
	if fld.Unit != "" {
		if d == "" {
			d = "(unit: " + fld.Unit + ")"
		} else {
			d += " (unit: " + fld.Unit + ")"
		}
	}
	return d
}

// messageFile emits <Message>.java: the public message class plus its reachable
// named-type classes (package-private) and the decode visitor.
func (g *gen) messageFile(m *ir.Message) []byte {
	f := &jfile{}
	g.header(f)
	f.line("package %s;", g.pkg)
	f.line("import org.sofabuffers.sofab.*;")
	f.line("import java.io.IOException;")
	f.line("import java.util.*;")
	f.blank()

	order := g.reachable(m)
	for _, key := range order {
		nt := g.schema.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitClass(f, g.typeName(key), nt.Fields, nt.Summary, false, false)
		}
	}
	g.emitClass(f, exported(m.Name), m.Fields, m.Summary, true, true)
	return f.bytes()
}

func (g *gen) emitClass(f *jfile, name string, fields []*ir.Field, summary string, isMessage, isPublic bool) {
	vis := ""
	if isPublic {
		vis = "public "
	}
	f.javadoc("", summary)
	f.line("%sclass %s {", vis, name)
	for _, fld := range fields {
		f.javadoc("    ", fieldDoc(fld))
		f.line("    public %s %s%s;", g.javaType(fld), javaIdent(fld.Name), g.javaInit(fld))
	}
	f.blank()

	// marshal
	f.line("    public void marshal(OStream os) throws IOException {")
	for _, fld := range fields {
		g.emitMarshal(f, fld)
	}
	f.line("    }")

	if isMessage {
		size, _ := g.maxSize(fields)
		f.line("    public static final int MAX_SIZE = %d;", size)
		f.line("    public byte[] encode() {")
		f.line("        try {")
		f.line("            byte[] buf = new byte[MAX_SIZE];")
		f.line("            OStream os = new OStream(buf);")
		f.line("            marshal(os);")
		f.line("            return Arrays.copyOf(buf, os.bytesUsed());")
		f.line("        } catch (IOException e) { throw new RuntimeException(e); }")
		f.line("    }")
		f.line("    public static %s decode(byte[] data) {", name)
		f.line("        %s m = new %s();", name, name)
		f.line("        try { new IStream().feed(data, new %sVisitor(m)); }", name)
		f.line("        catch (Exception e) { throw new RuntimeException(e); }")
		f.line("        return m;")
		f.line("    }")
	}
	f.line("}")
	f.blank()

	if isMessage {
		g.emitVisitor(f, name, fields)
	}
}

func (g *gen) emitMarshal(f *jfile, fld *ir.Field) {
	acc := "this." + javaIdent(fld.Name)
	var write string
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		write = fmt.Sprintf("os.writeUnsigned(%d, %s);", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		write = fmt.Sprintf("os.writeSigned(%d, %s);", fld.ID, acc)
	case ir.KindBool:
		write = fmt.Sprintf("os.writeBoolean(%d, %s);", fld.ID, acc)
	case ir.KindFP32:
		write = fmt.Sprintf("os.writeFp32(%d, %s);", fld.ID, acc)
	case ir.KindFP64:
		write = fmt.Sprintf("os.writeFp64(%d, %s);", fld.ID, acc)
	case ir.KindString:
		write = fmt.Sprintf("os.writeString(%d, %s == null ? \"\" : %s);", fld.ID, acc, acc)
	case ir.KindBlob:
		f.line("        os.writeBlob(%d, %s == null ? new byte[0] : %s);", fld.ID, acc, acc)
		return
	case ir.KindStruct, ir.KindUnion:
		f.line("        os.writeSequenceBegin(%d); (%s == null ? new %s() : %s).marshal(os); os.writeSequenceEnd();", fld.ID, acc, g.typeName(fld.Ref.Key), acc)
		return
	case ir.KindArray:
		g.emitMarshalArray(f, fld, acc)
		return
	}
	if g.omit {
		f.line("        if (%s) { %s }", g.javaOmitCond(fld), write)
	} else {
		f.line("        %s", write)
	}
}

func (g *gen) emitMarshalArray(f *jfile, fld *ir.Field, acc string) {
	g.marshalArray(f, "        ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0)
}

// marshalArray writes the array val as field idExpr. Numeric/enum/boolean/
// bitfield elements use the native array wire type (enum->signed, bool/bitfield->
// unsigned); string/blob/struct/union/array elements lower to a wrapper sequence
// whose child ids are the 0-based index (per MESSAGE_SPEC). Recurses for nested
// arrays, depth-suffixing loop vars to avoid collisions.
func (g *gen) marshalArray(f *jfile, ind, idExpr, val string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int) {
	iv := fmt.Sprintf("_i%d", depth)
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("%sos.writeArrayUnsigned(%s, Sbuf.toLongArray(%s));", ind, idExpr, val)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("%sos.writeArraySigned(%s, Sbuf.toLongArray(%s));", ind, idExpr, val)
	case ir.KindBool:
		f.line("%sos.writeArrayUnsigned(%s, Sbuf.boolToLongArray(%s));", ind, idExpr, val)
	case ir.KindFP32:
		f.line("%sos.writeArrayFp32(%s, Sbuf.toFloatArray(%s));", ind, idExpr, val)
	case ir.KindFP64:
		f.line("%sos.writeArrayFp64(%s, Sbuf.toDoubleArray(%s));", ind, idExpr, val)
	case ir.KindString:
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) os.writeString(%s, %s.get(%s) == null ? \"\" : %s.get(%s));", ind, iv, iv, val, iv, iv, val, iv, val, iv)
		f.line("%sos.writeSequenceEnd();", ind)
	case ir.KindBlob:
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) os.writeBlob(%s, %s.get(%s) == null ? new byte[0] : %s.get(%s));", ind, iv, iv, val, iv, iv, val, iv, val, iv)
		f.line("%sos.writeSequenceEnd();", ind)
	case ir.KindStruct, ir.KindUnion:
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) { os.writeSequenceBegin(%s); (%s.get(%s) == null ? new %s() : %s.get(%s)).marshal(os); os.writeSequenceEnd(); }", ind, iv, iv, val, iv, iv, val, iv, g.typeName(ref.Key), val, iv)
		f.line("%sos.writeSequenceEnd();", ind)
	case ir.KindArray:
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) {", ind, iv, iv, val, iv)
		g.marshalArray(f, ind+"    ", iv, fmt.Sprintf("%s.get(%s)", val, iv), items.Elem, items.ElemRef, items.ElemItems, depth+1)
		f.line("%s}", ind)
		f.line("%sos.writeSequenceEnd();", ind)
	}
}
