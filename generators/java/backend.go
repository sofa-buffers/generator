// Package java is the Java throughput backend (PLAN §6.4): classes with
// marshal over OStream and a flat-visitor decode (Visitor) against corelib-java.
// The Visitor is flat (sequenceBegin/end, no child visitors), so decode is a
// (location, id) state machine with a location stack — same as Rust/C#. Java has
// no unsigned ints, so all integers map to long (u64 handled via
// Long.toUnsignedString / parseUnsignedLong in the JSON harness).
package java

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

func init() { generator.Register(&Backend{}) }

// Backend implements generator.Backend for Java.
type Backend struct{}

func (*Backend) Lang() string { return "java" }

func (*Backend) Generate(s *ir.Schema, cfg map[string]any) ([]generator.File, error) {
	g := &gen{schema: s, pkg: cfgString(cfg, "package", "messages"), banner: cfgString(cfg, "tool_banner", "sbufgen")}
	dir := "src/main/java/" + strings.ReplaceAll(g.pkg, ".", "/") + "/"
	var files []generator.File
	files = append(files, generator.File{Path: dir + "Sbuf.java", Content: g.sbufSupport()})
	for _, m := range s.Messages {
		files = append(files, generator.File{Path: dir + exported(m.Name) + ".java", Content: g.messageFile(m)})
	}
	if cfgString(cfg, "emit", "sources") == "project" {
		files = append(files, g.projectFiles(s, cfg, dir)...)
	}
	return files, nil
}

type gen struct {
	schema *ir.Schema
	pkg    string
	banner string
}

type jfile struct{ b strings.Builder }

func (f *jfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}
func (f *jfile) blank()        { f.b.WriteByte('\n') }
func (f *jfile) bytes() []byte { return []byte(f.b.String()) }

// messageFile emits <Message>.java: the public message class plus its reachable
// named-type classes (package-private) and the decode visitor.
func (g *gen) messageFile(m *ir.Message) []byte {
	f := &jfile{}
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	f.line("package %s;", g.pkg)
	f.line("import org.sofabuffers.sofab.*;")
	f.line("import java.io.IOException;")
	f.line("import java.util.*;")
	f.blank()

	order := g.reachable(m)
	for _, key := range order {
		nt := g.schema.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitClass(f, g.typeName(key), nt.Fields, false, false)
		}
	}
	g.emitClass(f, exported(m.Name), m.Fields, true, true)
	return f.bytes()
}

func (g *gen) emitClass(f *jfile, name string, fields []*ir.Field, isMessage, isPublic bool) {
	vis := ""
	if isPublic {
		vis = "public "
	}
	f.line("%sclass %s {", vis, name)
	for _, fld := range fields {
		f.line("    public %s %s%s;", g.javaType(fld), fld.Name, g.javaInit(fld))
	}
	f.blank()

	// marshal
	f.line("    public void marshal(OStream os) throws IOException {")
	for _, fld := range fields {
		g.emitMarshal(f, fld)
	}
	f.line("    }")

	if isMessage {
		size, _ := g.maxSize(fields)
		f.line("    public static final int MAX_SIZE = %d;", size)
		f.line("    public byte[] encode() {")
		f.line("        try {")
		f.line("            byte[] buf = new byte[MAX_SIZE];")
		f.line("            OStream os = new OStream(buf);")
		f.line("            marshal(os);")
		f.line("            return Arrays.copyOf(buf, os.bytesUsed());")
		f.line("        } catch (IOException e) { throw new RuntimeException(e); }")
		f.line("    }")
		f.line("    public static %s decode(byte[] data) {", name)
		f.line("        %s m = new %s();", name, name)
		f.line("        try { new IStream().feed(data, new %sVisitor(m)); }", name)
		f.line("        catch (Exception e) { throw new RuntimeException(e); }")
		f.line("        return m;")
		f.line("    }")
	}
	f.line("}")
	f.blank()

	if isMessage {
		g.emitVisitor(f, name, fields)
	}
}

func (g *gen) emitMarshal(f *jfile, fld *ir.Field) {
	acc := "this." + fld.Name
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("        os.writeUnsigned(%d, %s);", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("        os.writeSigned(%d, %s);", fld.ID, acc)
	case ir.KindBool:
		f.line("        os.writeBoolean(%d, %s);", fld.ID, acc)
	case ir.KindFP32:
		f.line("        os.writeFp32(%d, %s);", fld.ID, acc)
	case ir.KindFP64:
		f.line("        os.writeFp64(%d, %s);", fld.ID, acc)
	case ir.KindString:
		f.line("        os.writeString(%d, %s == null ? \"\" : %s);", fld.ID, acc, acc)
	case ir.KindBlob:
		f.line("        os.writeBlob(%d, %s == null ? new byte[0] : %s);", fld.ID, acc, acc)
	case ir.KindStruct, ir.KindUnion:
		f.line("        os.writeSequenceBegin(%d); (%s == null ? new %s() : %s).marshal(os); os.writeSequenceEnd();", fld.ID, acc, g.typeName(fld.Ref.Key), acc)
	case ir.KindArray:
		g.emitMarshalArray(f, fld, acc)
	}
}

func (g *gen) emitMarshalArray(f *jfile, fld *ir.Field, acc string) {
	switch fld.Elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64:
		f.line("        os.writeArrayUnsigned(%d, Sbuf.toLongArray(%s));", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64:
		f.line("        os.writeArraySigned(%d, Sbuf.toLongArray(%s));", fld.ID, acc)
	case ir.KindFP32:
		f.line("        os.writeArrayFp32(%d, Sbuf.toFloatArray(%s));", fld.ID, acc)
	case ir.KindFP64:
		f.line("        os.writeArrayFp64(%d, Sbuf.toDoubleArray(%s));", fld.ID, acc)
	case ir.KindString:
		f.line("        os.writeSequenceBegin(%d);", fld.ID)
		f.line("        for (int _i = 0; _i < %s.size(); _i++) os.writeString(_i, %s.get(_i) == null ? \"\" : %s.get(_i));", acc, acc, acc)
		f.line("        os.writeSequenceEnd();")
	case ir.KindBlob:
		f.line("        os.writeSequenceBegin(%d);", fld.ID)
		f.line("        for (int _i = 0; _i < %s.size(); _i++) os.writeBlob(_i, %s.get(_i) == null ? new byte[0] : %s.get(_i));", acc, acc, acc)
		f.line("        os.writeSequenceEnd();")
	}
}
