// Package java is the Java throughput backend (PLAN §6.4): classes with
// marshal over OStream and a flat-visitor decode (Visitor) against corelib-java.
// The Visitor is flat (sequenceBegin/end, no child visitors), so decode is a
// (location, id) state machine with a location stack — same as Rust/C#. Java has
// no unsigned ints, so all integers map to long (u64 handled via
// Long.toUnsignedString / parseUnsignedLong in the JSON harness).
package java

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

func init() { generator.Register(&Backend{}) }

// Backend implements generator.Backend for Java.
type Backend struct{}

func (*Backend) Lang() string { return "java" }

func (*Backend) Generate(s *ir.Schema, cfg map[string]any) ([]generator.File, error) {
	g := &gen{schema: s, pkg: cfgString(cfg, "package", "message"), banner: cfgString(cfg, "tool_banner", "sofabgen"), license: generator.LicenseID(cfg), limits: resolveLimits(s, cfg)}
	dir := "src/main/java/" + strings.ReplaceAll(g.pkg, ".", "/") + "/"
	var files []generator.File
	files = append(files, generator.File{Path: dir + "Sbuf.java", Content: g.sbufSupport()})
	for _, m := range s.Messages {
		files = append(files, generator.File{Path: dir + exported(m.Name) + ".java", Content: g.messageFile(m)})
	}
	if cfgString(cfg, "emit", "sources") == "project" {
		files = append(files, g.projectFiles(s, cfg, dir)...)
	}
	return files, nil
}

type gen struct {
	schema  *ir.Schema
	pkg     string
	banner  string
	license string // SPDX id, "" to omit the header line
	limits  limitSet
}

// limitSet is the receiver-side decode-limit configuration (generator#102). An
// entry is active only when its key is configured AND the schema actually has
// an unbounded field of that kind — otherwise the option would be inert and no
// limit plumbing is emitted. Unlike backends whose corelib enforces the limits
// globally per decode (Go, Python, TypeScript), the Java visitor guards each
// unbounded field individually, so the configured value is emitted as-is —
// schema-bounded fields never see it (they keep their generator#100 guard).
type limitSet struct {
	arrayCount, stringLen, blobLen int64
	arrayHas, stringHas, blobHas   bool
}

// resolveLimits reads the max_dyn_* config keys and gates them on the schema's
// bounds (see limitSet).
func resolveLimits(s *ir.Schema, cfg map[string]any) limitSet {
	var all []*ir.Field
	for _, m := range s.Messages {
		all = append(all, m.Fields...)
	}
	b := ir.Bounds(all)
	var l limitSet
	if v, ok := cfgLimit(cfg, "max_dyn_array_count"); ok && b.HasDynArray {
		l.arrayCount, l.arrayHas = v, true
	}
	if v, ok := cfgLimit(cfg, "max_dyn_string_len"); ok && b.HasDynString {
		l.stringLen, l.stringHas = v, true
	}
	if v, ok := cfgLimit(cfg, "max_dyn_blob_len"); ok && b.HasDynBlob {
		l.blobLen, l.blobHas = v, true
	}
	return l
}

// header writes the @generated banner and, when a license is set, an SPDX line.
func (g *gen) header(f *jfile) {
	f.line("// Code generated by %s; DO NOT EDIT.", g.banner)
	if g.license != "" {
		f.line("// SPDX-License-Identifier: %s", g.license)
	}
}

type jfile struct{ b strings.Builder }

func (f *jfile) line(format string, args ...any) {
	fmt.Fprintf(&f.b, format, args...)
	f.b.WriteByte('\n')
}
func (f *jfile) blank()        { f.b.WriteByte('\n') }
func (f *jfile) bytes() []byte { return []byte(f.b.String()) }

// javadoc writes a Javadoc doc comment for text at the given indent, or nothing
// when text is empty. Single-line text becomes `/** text */`; multi-line text is
// expanded across lines. Any `*/` in the text is neutralised to `* /` so it can
// never close the comment early; UTF-8 passes through byte-for-byte.
func (f *jfile) javadoc(indent, text string) {
	if text == "" {
		return
	}
	text = strings.ReplaceAll(text, "*/", "* /")
	lines := strings.Split(strings.TrimRight(text, "\n"), "\n")
	if len(lines) == 1 {
		f.line("%s/** %s */", indent, lines[0])
		return
	}
	f.line("%s/**", indent)
	for _, ln := range lines {
		if ln == "" {
			f.line("%s *", indent)
		} else {
			f.line("%s * %s", indent, ln)
		}
	}
	f.line("%s */", indent)
}

// fieldDoc is the Javadoc body for a field: its description, with a unit suffix
// appended (or used alone). Empty when the field carries neither.
func fieldDoc(fld *ir.Field) string {
	d := fld.Description
	if fld.Unit != "" {
		if d == "" {
			d = "(unit: " + fld.Unit + ")"
		} else {
			d += " (unit: " + fld.Unit + ")"
		}
	}
	return d
}

// messageFile emits <Message>.java: the public message class plus its reachable
// named-type classes (package-private) and the decode visitor.
func (g *gen) messageFile(m *ir.Message) []byte {
	f := &jfile{}
	g.header(f)
	f.line("package %s;", g.pkg)
	f.line("import org.sofabuffers.sofab.*;")
	f.line("import java.io.IOException;")
	f.line("import java.util.*;")
	f.blank()

	order := g.reachable(m)
	for _, key := range order {
		nt := g.schema.Named[key]
		if nt.Category == ir.CatStruct || nt.Category == ir.CatUnion {
			g.emitClass(f, g.typeName(key), nt.Fields, nt.Summary, false, false)
		}
	}
	g.emitClass(f, exported(m.Name), m.Fields, m.Summary, true, true)
	return f.bytes()
}

func (g *gen) emitClass(f *jfile, name string, fields []*ir.Field, summary string, isMessage, isPublic bool) {
	vis := ""
	if isPublic {
		vis = "public "
	}
	f.javadoc("", summary)
	f.line("%sclass %s {", vis, name)
	for _, fld := range fields {
		f.javadoc("    ", fieldDoc(fld))
		f.line("    public %s %s%s;", g.javaType(fld), javaIdent(fld.Name), g.javaInit(fld))
	}
	f.blank()

	// marshal
	f.line("    public void marshal(OStream os) throws IOException {")
	for _, fld := range fields {
		g.emitMarshal(f, fld)
	}
	f.line("    }")

	if isMessage {
		size, _ := g.maxSize(fields)
		f.line("    public static final int MAX_SIZE = %d;", size)
		f.line("    // Per-thread scratch buffer: encode() marshals into it and returns an")
		f.line("    // exact-size copy, so the worst-case buffer is not re-allocated (and")
		f.line("    // zeroed) on every call. Do not call encode() reentrantly from a")
		f.line("    // marshal() override on the same thread.")
		f.line("    private static final ThreadLocal<byte[]> ENC_BUF =")
		f.line("        ThreadLocal.withInitial(() -> new byte[MAX_SIZE]);")
		f.line("    public byte[] encode() {")
		f.line("        try {")
		f.line("            byte[] buf = ENC_BUF.get();")
		f.line("            OStream os = new OStream(buf);")
		f.line("            marshal(os);")
		f.line("            return Arrays.copyOf(buf, os.bytesUsed());")
		f.line("        } catch (IOException e) { throw new RuntimeException(e); }")
		f.line("    }")
		// Best-effort decode: kept for back-compat. feed() suspends without
		// throwing on a truncated message, so this cannot tell COMPLETE from
		// INCOMPLETE — prefer tryDecode when that verdict matters
		// (generator#105).
		f.line("    public static %s decode(byte[] data) {", name)
		f.line("        %s m = new %s();", name, name)
		f.line("        try { new IStream().feed(data, new %sVisitor(m)); }", name)
		f.line("        catch (Exception e) { throw new RuntimeException(e); }")
		f.line("        return m;")
		f.line("    }")
		// Status-surfacing one-shot decode (MESSAGE_SPEC §7): fills the
		// caller-provided instance with the fields decoded so far and returns
		// the corelib's terminal DecodeStatus — COMPLETE at a clean field
		// boundary, INCOMPLETE when the bytes end inside a field or an open
		// sequence. Malformed input throws SofabException (INVALID) from feed;
		// the caller owns end-of-input and decides whether a trailing
		// INCOMPLETE is a truncation error.
		f.line("    public static DecodeStatus tryDecode(byte[] data, %s out) throws SofabException {", name)
		f.line("        IStream is = new IStream();")
		f.line("        is.feed(data, new %sVisitor(out));", name)
		f.line("        return is.status();")
		f.line("    }")
	}
	f.line("}")
	f.blank()

	if isMessage {
		g.emitVisitor(f, name, fields)
	}
}

func (g *gen) emitMarshal(f *jfile, fld *ir.Field) {
	acc := "this." + javaIdent(fld.Name)
	var write string
	switch fld.Kind {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		write = fmt.Sprintf("os.writeUnsigned(%d, %s);", fld.ID, acc)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		write = fmt.Sprintf("os.writeSigned(%d, %s);", fld.ID, acc)
	case ir.KindBool:
		write = fmt.Sprintf("os.writeBoolean(%d, %s);", fld.ID, acc)
	case ir.KindFP32:
		write = fmt.Sprintf("os.writeFp32(%d, %s);", fld.ID, acc)
	case ir.KindFP64:
		write = fmt.Sprintf("os.writeFp64(%d, %s);", fld.ID, acc)
	case ir.KindString:
		write = fmt.Sprintf("os.writeString(%d, %s == null ? \"\" : %s);", fld.ID, acc, acc)
	case ir.KindBlob:
		// A blob is a leaf: omit when equal to its default (empty when none).
		// With an empty default the content compare degenerates to a length
		// check, sparing an Arrays.equals against a fresh byte[0] per call.
		if def := g.javaDefaultValue(fld); def == "new byte[0]" || def == "Sbuf.EMPTY_BYTES" {
			f.line("        if (%s == null || %s.length != 0) { os.writeBlob(%d, %s == null ? new byte[0] : %s); }", acc, acc, fld.ID, acc, acc)
			return
		}
		f.line("        if (!Arrays.equals(%s, %s)) { os.writeBlob(%d, %s == null ? new byte[0] : %s); }", acc, g.javaDefaultValue(fld), fld.ID, acc, acc)
		return
	case ir.KindStruct, ir.KindUnion:
		// A sequence is always framed; its child fields are omitted per-field by
		// the nested marshal (MESSAGE_SPEC S2). An all-default nested object thus
		// becomes an empty wrapper sequence, not a dropped field.
		f.line("        os.writeSequenceBegin(%d); (%s == null ? new %s() : %s).marshal(os); os.writeSequenceEnd();", fld.ID, acc, g.typeName(fld.Ref.Key), acc)
		return
	case ir.KindArray:
		g.emitMarshalArray(f, fld, acc)
		return
	}
	// Scalar/string/enum/bitfield leaf: always omit when equal to the default;
	// sparse encoding is canonical (MESSAGE_SPEC S2) and the decoder reconstructs
	// the omitted field from its default.
	f.line("        if (%s) { %s }", g.javaOmitCond(fld), write)
}

func (g *gen) emitMarshalArray(f *jfile, fld *ir.Field, acc string) {
	// A native scalar array is a leaf field: omit it when equal to its default
	// (materialized at construction), else when empty. A composite/dynamic-element
	// array is a wrapper sequence and is always framed (never whole-omitted).
	if primitiveArrayElem(fld.Elem) {
		// Primitive array (long[]/float[]/double[]): omit when equal to its default
		// (Arrays.equals), else when empty; write straight to the OStream primitive
		// overload with no Sbuf box/unbox temporary.
		if lit, ok := g.javaPrimArrayLiteral(fld); ok {
			f.line("        if (!java.util.Arrays.equals(%s, %s)) {", acc, lit)
		} else {
			f.line("        if (%s != null && %s.length != 0) {", acc, acc)
		}
		g.marshalArray(f, "            ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, true)
		f.line("        }")
		return
	}
	if nativeArrayElem(fld.Elem) { // boolean array (boxed List<Boolean>)
		if def, ok := g.javaNativeArrayLiteral(fld); ok {
			f.line("        if (!%s.equals(%s)) {", def, acc)
		} else {
			f.line("        if (%s != null && !%s.isEmpty()) {", acc, acc)
		}
		g.marshalArray(f, "            ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, false)
		f.line("        }")
		return
	}
	g.marshalArray(f, "        ", fmt.Sprintf("%d", fld.ID), acc, fld.Elem, fld.ElemRef, fld.ElemItems, 0, false)
}

// marshalArray writes the array val as field idExpr. Numeric/enum/boolean/
// bitfield elements use the native array wire type (enum->signed, bool/bitfield->
// unsigned); string/blob/struct/union/array elements lower to a wrapper sequence
// whose child ids are the 0-based index (per MESSAGE_SPEC). Recurses for nested
// arrays, depth-suffixing loop vars to avoid collisions.
// prim is true only for a direct primitive array field (`val` is a
// long[]/float[]/double[]); it writes straight to the OStream overload. When
// false, `val` is a boxed List and is unboxed via a Sbuf.to*Array temporary —
// the case for boolean arrays and for a nested array's inner rows (which stay
// List<...>).
func (g *gen) marshalArray(f *jfile, ind, idExpr, val string, elem ir.Kind, ref *ir.TypeRef, items *ir.ArrayElem, depth int, prim bool) {
	iv := fmt.Sprintf("_i%d", depth)
	longs := "Sbuf.toLongArray(" + val + ")"
	floats := "Sbuf.toFloatArray(" + val + ")"
	doubles := "Sbuf.toDoubleArray(" + val + ")"
	if prim {
		longs, floats, doubles = val, val, val
	}
	switch elem {
	case ir.KindU8, ir.KindU16, ir.KindU32, ir.KindU64, ir.KindBitfield:
		f.line("%sos.writeArrayUnsigned(%s, %s);", ind, idExpr, longs)
	case ir.KindI8, ir.KindI16, ir.KindI32, ir.KindI64, ir.KindEnum:
		f.line("%sos.writeArraySigned(%s, %s);", ind, idExpr, longs)
	case ir.KindBool:
		f.line("%sos.writeArrayUnsigned(%s, Sbuf.boolToLongArray(%s));", ind, idExpr, val)
	case ir.KindFP32:
		f.line("%sos.writeArrayFp32(%s, %s);", ind, idExpr, floats)
	case ir.KindFP64:
		f.line("%sos.writeArrayFp64(%s, %s);", ind, idExpr, doubles)
	case ir.KindString:
		// A string element is a leaf: omit it when equal to the element default
		// (empty), leaving an id gap the decoder restores (MESSAGE_SPEC S2).
		ev := fmt.Sprintf("_e%d", depth)
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) { String %s = %s.get(%s); if (%s != null && !%s.isEmpty()) os.writeString(%s, %s); }", ind, iv, iv, val, iv, ev, val, iv, ev, ev, iv, ev)
		f.line("%sos.writeSequenceEnd();", ind)
	case ir.KindBlob:
		// A blob element is a leaf: omit it when equal to the element default
		// (empty), leaving an id gap the decoder restores (MESSAGE_SPEC S2).
		ev := fmt.Sprintf("_e%d", depth)
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) { byte[] %s = %s.get(%s); if (%s != null && %s.length != 0) os.writeBlob(%s, %s); }", ind, iv, iv, val, iv, ev, val, iv, ev, ev, iv, ev)
		f.line("%sos.writeSequenceEnd();", ind)
	case ir.KindStruct, ir.KindUnion:
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) { os.writeSequenceBegin(%s); (%s.get(%s) == null ? new %s() : %s.get(%s)).marshal(os); os.writeSequenceEnd(); }", ind, iv, iv, val, iv, iv, val, iv, g.typeName(ref.Key), val, iv)
		f.line("%sos.writeSequenceEnd();", ind)
	case ir.KindArray:
		f.line("%sos.writeSequenceBegin(%s);", ind, idExpr)
		f.line("%sfor (int %s = 0; %s < %s.size(); %s++) {", ind, iv, iv, val, iv)
		g.marshalArray(f, ind+"    ", iv, fmt.Sprintf("%s.get(%s)", val, iv), items.Elem, items.ElemRef, items.ElemItems, depth+1, false)
		f.line("%s}", ind)
		f.line("%sos.writeSequenceEnd();", ind)
	}
}
