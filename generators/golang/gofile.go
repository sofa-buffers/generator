// Package golang is the Go throughput backend (PLAN §6.4): it emits one struct
// per object with Marshal (streaming Encoder) and a pull-parser Unmarshal
// (Decoder.Next/Skip + typed readers) against corelib-go. Generated code is a
// thin, allocation-light wrapper; the corelib owns the wire format.
package golang

import (
	"fmt"
	"go/format"
	"sort"
	"strings"
)

// gofile is the small Builder for a Go source file (PLAN §8.4): intent-level
// line output + managed imports, no ad-hoc cross-file concatenation.
type gofile struct {
	pkg     string
	imports map[string]bool
	body    strings.Builder
}

func newGoFile(pkg string) *gofile {
	return &gofile{pkg: pkg, imports: map[string]bool{}}
}

func (f *gofile) imp(path string) { f.imports[path] = true }

func (f *gofile) line(format string, args ...any) {
	fmt.Fprintf(&f.body, format, args...)
	f.body.WriteByte('\n')
}

func (f *gofile) blank() { f.body.WriteByte('\n') }

// bytes assembles the final file: banner + package + sorted imports + body.
func (f *gofile) bytes(banner string) []byte {
	var b strings.Builder
	b.WriteString("// Code generated by ")
	b.WriteString(banner)
	b.WriteString("; DO NOT EDIT.\n")
	b.WriteString("// SPDX-License-Identifier: MIT\n\n")
	b.WriteString("package ")
	b.WriteString(f.pkg)
	b.WriteString("\n\n")
	if len(f.imports) > 0 {
		paths := make([]string, 0, len(f.imports))
		for p := range f.imports {
			paths = append(paths, p)
		}
		sort.Strings(paths)
		b.WriteString("import (\n")
		for _, p := range paths {
			fmt.Fprintf(&b, "\t%q\n", p)
		}
		b.WriteString(")\n\n")
	}
	b.WriteString(f.body.String())
	// Format with go/format so generated Go is gofmt-clean and deterministic.
	// If formatting fails (a codegen bug), return the raw source so the error
	// surfaces at compile time rather than being hidden.
	src := []byte(b.String())
	if formatted, err := format.Source(src); err == nil {
		return formatted
	}
	return src
}
