package golang

import (
	"fmt"
	"strings"

	"github.com/sofa-buffers/generator/internal/generator"
	"github.com/sofa-buffers/generator/internal/ir"
)

// projectFiles scaffolds a buildable Go module with an encode/decode JSON
// harness (PLAN §9.1). The harness uses stdlib encoding/json — the generated
// structs carry json tags with the canonical schema field names — so no JSON
// codegen is needed beyond the struct tags.
func (g *gen) projectFiles(s *ir.Schema, cfg map[string]any) []generator.File {
	modPath := cfgString(cfg, "module_path", "example.com/generated")
	goVersion := cfgString(cfg, "go_version", "1.21")
	return []generator.File{
		{Path: "go.mod", Content: []byte(g.goMod(modPath, goVersion))},
		{Path: "harness/main.go", Content: g.harness(s, modPath)},
		{Path: "README.md", Content: []byte(g.readme(s))},
	}
}

func (g *gen) goMod(modPath, goVersion string) string {
	return fmt.Sprintf(`// @generated by sofabgen
module %s

go %s

require %s v0.0.0

replace %s => %s
`, modPath, goVersion, corelibImport, corelibImport, "${SOFAB_GO_CORELIB}")
}

func (g *gen) readme(s *ir.Schema) string {
	var b strings.Builder
	b.WriteString("# Generated SofaBuffers Go module\n\n")
	b.WriteString("`@generated by sofabgen`. Encode/decode harness over `corelib-go`.\n\n")
	b.WriteString("```sh\n# point the replace at a corelib-go checkout, then:\n")
	b.WriteString("go run ./harness encode <Message> < in.json   # JSON -> bytes\n")
	b.WriteString("go run ./harness decode <Message> < bytes.bin # bytes -> JSON\n```\n")
	return b.String()
}

// harness emits the uniform encode/decode JSON CLI for the Go module.
func (g *gen) harness(s *ir.Schema, modPath string) []byte {
	f := newGoFile("main")
	f.imp("encoding/json")
	f.imp("fmt")
	f.imp("io")
	f.imp("os")
	f.imp(modPath + "/" + g.pkg)

	pkgAlias := g.pkg
	f.line("func main() {")
	f.line("\tif len(os.Args) < 2 {")
	f.line("\t\tfmt.Fprintln(os.Stderr, \"usage: harness <encode|decode> [Message]\")")
	f.line("\t\tos.Exit(2)")
	f.line("\t}")
	f.line("\tmode := os.Args[1]")
	f.line("\tmsg := %q", defaultMessage(s))
	f.line("\tif len(os.Args) > 2 {")
	f.line("\t\tmsg = os.Args[2]")
	f.line("\t}")
	f.line("\tin, _ := io.ReadAll(os.Stdin)")
	f.line("\tswitch msg {")
	for _, m := range s.Messages {
		typeName := exported(m.Name)
		f.line("\tcase %q:", m.Name)
		f.line("\t\tif mode == \"encode\" {")
		f.line("\t\t\tobj := %s.New%s()", pkgAlias, typeName)
		f.line("\t\t\tif err := json.Unmarshal(in, obj); err != nil {")
		f.line("\t\t\t\tfail(err)")
		f.line("\t\t\t}")
		f.line("\t\t\tb, err := obj.Encode()")
		f.line("\t\t\tif err != nil {")
		f.line("\t\t\t\tfail(err)")
		f.line("\t\t\t}")
		f.line("\t\t\tos.Stdout.Write(b)")
		f.line("\t\t} else if mode == \"decode\" {")
		f.line("\t\t\tobj, err := %s.Decode%s(in)", pkgAlias, typeName)
		f.line("\t\t\tif err != nil {")
		f.line("\t\t\t\tfail(err)")
		f.line("\t\t\t}")
		f.line("\t\t\tout, _ := json.Marshal(obj)")
		f.line("\t\t\tos.Stdout.Write(out)")
		f.line("\t\t\tfmt.Fprintln(os.Stdout)")
		f.line("\t\t} else {")
		f.line("\t\t\tfail(fmt.Errorf(\"unknown mode %%q\", mode))")
		f.line("\t\t}")
	}
	f.line("\tdefault:")
	f.line("\t\tfail(fmt.Errorf(\"unknown message %%q\", msg))")
	f.line("\t}")
	f.line("}")
	f.blank()
	f.line("func fail(err error) {")
	f.line("\tfmt.Fprintln(os.Stderr, \"error:\", err)")
	f.line("\tos.Exit(1)")
	f.line("}")
	return f.bytes(g.banner)
}

func defaultMessage(s *ir.Schema) string {
	if len(s.Messages) > 0 {
		return s.Messages[0].Name
	}
	return ""
}
